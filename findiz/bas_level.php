                                            <h3>Для чего нужны фондовые рынки?</h3>

                                            <p>
                                                Фондовый рынок необходим:
                                                <li>для того, чтобы обеспечивать процесс воспроизводства капитала;</li>
                                                <li> для продажи ценных бумаг на фондовом рынке и для мобилизации и концентрации свободных денежных средств — капиталов и накоплений;</li>
                                                <li>для инвестирования в государственные ценные бумаги и ценные бумаги хозяйствующих организаций;</li>
                                                <li>для обеспечения высокого уровня ликвидности вложений в ценные бумаги.</li>
                                            </p>
                                            
                                            <h3>Взаимодействие продавца и покупателя на фондовом рынке</h3>

                                            <p>
                                                Фондовый рынок — это реальная возможность увеличения капитала для предприятия. Когда эмитент продаёт ценные бумаги, он получает денежные средства на развитие предприятия. Когда инвестор покупает ценные бумаги, он передаёт денежные средства эмитенту на развитие предприятия.
                                            </p>

                                            <p>
                                                Инвестор может получить прибыль от покупки ценной бумаги двумя способами:
                                                <li>продав ценную бумагу по более высокой цене, чем цена покупки;</li>
                                                <li>получая доход от владения ценной бумагой (в зависимости от типа ценной бумаги доход может быть в виде фиксированной суммы, фиксированного процента, может зависеть от прибыли предприятия).</li>
                                            </p>



