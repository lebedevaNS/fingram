                                            <p>
                                                Для большей части населения в нашей стране основным видом источника доходов является заработная плата, которую выплачивает работнику работодатель. Отношения между работником и работодателем оформляются через правовой документ — трудовой договор, который регламентирует такие вопросы, как срок действия договора, размер и порядок выплаты заработной платы, порядок выплаты премий, права и обязанности работника и работодателя, установление испытательного срока и многое другое.
                                            </p>

