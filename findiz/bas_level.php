                                            <p>
                                                Знаете, когда вы станете по-настоящему взрослыми? Когда с вас начнут регулярно брать налоги. И это вовсе не шутка. Ведь человек становится взрослым, когда начинает самостоятельно получать доходы, зарабатывать себе на жизнь. А практически с любых доходов государство берёт (или, как обычно говорят, взимает) налог — обязательную плату в свою пользу. Вот и получается, что вся ваша взрослая жизнь (чаще всего после 16 лет) будет связана с необходимостью регулярно платить налоги.
                                            </p>
                                        
                                            <p>
                                                <i>
                                                    <b>Налоги</b> — это безвозмездный платёж, который специ- альные органы государства взимают с физических и юридических лиц по установленным законом основаниям.
                                                </i>
                                            </p>
                                        
                                            <p>
                                                Известное правило, что <i>незнание законов не освобождает от ответственности за их неисполнение, в полной мере относится и к сфере налогов.</i> Если человек не уплатил налог просто потому, что не знал, что должен это сделать, государство не только всё равно взыщет налог, но и накажет штрафом (он называется <b>пеня</b>) за неуплату в срок. Причём величина пени со временем растёт, так что просрочка уплаты налога может обойтись очень дорого.
                                            </p>
                                        
                                            <img src="assets/images/nalog.png" width="350" height="350" style="margin-bottom: 40px;">

                                            <p>
                                                С 1999 г. каждому человеку с рождения присваивается так называемый <b>идентификационный номер налогоплательщика (ИНН)</b>, состоящий из 12 цифр. Сегодня в России ИНН является цифровым именем (идентификатором) человека в регистрационных базах данных населения.
                                            </p>

                                            <p>
                                                Узнать свой ИНН можно в районной налоговой инспекции по месту прописки, подав заявление и приложив к нему копию паспорта. Через пять рабочих дней вам выдадут документ, содержащий ваш идентификационный номер и прочие личные данные.
                                            </p>

                                            <p>
                                                При наличии доступа в Интернет и паспорта узнать свой ИНН можно и онлайн. Для этого надо на сайте ФНС ввести в форму запроса серию и номер своего паспорта, имя, фамилию и отчество, а также дату рождения. Соответственно если вы внесли все эти данные и у вас уже есть какие-то налоговые обязательства перед государством, ваш ИНН после такого поиска появится на экране.
                                            </p>

                                            <p>
                                                Что означают эти загадочные 12 цифр? Первые две — код региона, где вы живёте, вторые две — номер выдавшей ИНН налоговой инспекции, следующие шесть — индивидуальные ваши, последние две — контрольные цифры, их используют для проверки правильности записи данных.
                                            </p>

                                            <p>
                                                Получение ИНН — обязательная процедура для любого налогоплательщика, причём не только для частного предпринимателя, подающего налоговую отчётность, но и для обычного человека, просто работающего по найму. Сейчас при приёме практически на любую работу от человека требуют сообщить свой идентификационный номер, так как работодатель платит государству налог на каждого своего сотрудника. И когда собственник фирмы подаёт документы на своих работников в налоговые органы, в декларации он упоминает всех официально зарегистрированных наёмных работников с их ИНН. Это позволяет налоговым органам следить за уплатой налогов всеми гражданами государства.
                                            </p>
                                        
                                            <img src="assets/images/tax.png" width="560" height="460">

                                            <p>
                                                Кстати, при изменении своих паспортных данных (к примеру, если после свадьбы один из молодожёнов берёт фамилию супруга) нужно получить новый документ, подтверждающий идентификационный номер с учётом этих изменений. Правда, сами цифры в ИНН останутся прежними.
                                            </p>
                                        
                                            <p>
                                                В цифровом мире, в котором мы теперь живём, ИНН облегчает поиск информации о человеке, не даёт ему «потеряться» или скрыть какие-либо юридически зарегистрированные факты своей биографии.Существование такой единой базы данных удобно не только государственным службам, но и рядовому человеку. Теперь получить какую-либо справку стало быстрее и проще, а определённую информацию можно узнать и в режиме онлайн на соответствующих ресурсах. Так, указав свой ИНН, можно узнать на сайте Федеральной налоговой службы (ФНС), нет ли у вас какой-то налоговой задолженности. Вы о ней можете даже не подозревать (например, письмо с извещением о начисленном налоге не дошло до адресата), а штрафные пени всё равно уже будут начисляться.
                                            </p>
                                        
                                            <p>
                                                Платить налоги необходимо не только потому, что за их неуплату налагаются штрафы и пени, но и потому, что во многом налоги обеспечивают доходную часть государственного бюджета, т. е. государство за счёт налогов организует оборону страны, охрану порядка, создаёт образовательные, медицинские, культурные и спортивные объекты, строит дороги, мосты, парки, осуществляет социальную поддержку определённых категорий граждан, вкладывает деньги в развитие всех сфер жизни общества.
                                            </p>
                                        
