                                                        <i>С вашей заработной платы будет взиматься подоходный налог. Например, если по трудовому договору ваша заработная плата составит 20 тыс. р., то из них 2600 р. будет удержано на уплату налога и перечислено государству. Когда родители купят вам автомобиль, вы будете обязаны уплачивать транспортный налог. Допустим, это будет автомобиль с мощностью двигателя 130 л.с. Если вы живёте, к примеру, в Москве, то сумма этого налога составит 4550 р., а если, скажем, в Смоленске, то 2080 р. Получив от бабушки в подарок земельный участок кадастровой стоимостью, допустим, 50 тыс. р., вам придётся платить налог 150 р. А на подаренную квартиру инвентаризационной стоимостью 400 тыс. р. налог составит 400 000 х 0,003 = 1200 р.</i>
