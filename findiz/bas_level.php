                                            <p>
                                                Особого внимания заслуживает раздел «Права и обязанности сторон». В первую очередь это должностные обязанности работника. Для многих профессий в нашей стране разработаны <b>квалификационные справочники и профессиональные стандарты</b>, в которых содержится информация о должностных обязанностях и требованиях, предъявляемых к уровню знаний и квалификации работников в соответствии с занимаемой должностью. Должностные обязанности работника, указанные в трудовом договоре, должны соответствовать квалификационным справочникам и профессиональным стандартам.
                                            </p>

                                            <p>
                                                Кроме того, к обязанностям работников относят требование соблюдения конфиденциальности и об уведомлении работодателя за недели в письменной форме о намерении уволиться. При заключении трудового договора необходимо обратить внимание на то, чтобы в разделе «Обязанности работодателя» было прописано, что работодатель обязан своевременно перечислять налоговые отчисления из заработной платы сотрудников в соответствующие фонды и бюджеты; обеспечить безопасность рабочего места сотрудников; оплачивать пребывание работника на больничном.
                                            </p>

                                            <h3>Размер заработной платы, указанной в трудовом договоре</h3>

                                            <p>
                                                В трудовом договоре указывают более низкий размер заработной платы, чем это оговорено на словах. При заключении трудового договора работник должен убедиться, что в трудовом договоре указана именно та заработная плата, которую обещал работодатель при устройстве на работу.
                                            </p>

                                            <p>
                                                Занижение заработной платы в трудовом договоре невыгодно работнику, поскольку:
                                                <li>Hедобросовестные работодатели могут «оштрафовать» работника и не доплатить ему зарплату, чего нельзя сделать, если заработная плата выплачивается официально, а не в конверте</li>
                                                <li> При уходе в отпуск работнику начисляют отпускные только с официальной зарплаты.</li>
                                                <li> Больничный лист оплачивается, исходя из размера заработной платы, указанной в трудовом договоре.</li>
                                                <li>Декретный отпуск будет оплачиваться, исходя из размера заработной платы, указанной в трудовом договоре.</li>
                                                <li>При расчёте пенсии по достижении пенсионного возраста её размер будет значительно ниже, чем у тех, кто получал официальную зарплату в таком же размере.</li>
                                            </p>

                                            <h3>Испытательный срок при приёме на работу</h3>

                                            <p>
                                                При приёме на работу нового сотрудника работодатель имеет право установить испытательный срок. Установление испытательного срока для проверки соответствия сотрудника поручаемой работе оговаривается в трудовом договоре
