											<p>
												<i>
													Людмила Анатольевна работает учителем литературы в сельской школе уже более 30 лет. Недавно было собрание всех работников школы, на котором объявили, что со следующего месяца все работники будут получать зарплату «на карту». Выступал работник банка и предлагал оформить всем желающим не только дебетовую, но ещё и кредитную карту. Людмила Анатольевна пришла домой и рассказала об этом мужу, который тоже, как и она, был несведущ в финансовых вопросах. Они оба сомневались, что это — хорошая новость, так как понятия не имели, как этими картами пользоваться и зачем они им нужны.

												</i>
											</p>
											<h4>Что же такое кредитные карты, как ими пользоваться и чем дебетовые карты отличаются от кредитных?</h4>
										</div>
									</blockquote><br>

									<p>
										Банковские карты являются неотъемлемой частью современной жизни. Конечно, неизвестность всегда пугает, как в случае пожилой сельской учительницы. Но раз мы не можем обойтись без банков и банковских карт, необходимо научиться грамотно ими пользоваться. Кроме работы со сбережениями банки часто предлагают услуги по облегчению платежей с помощью банковских карт. Такие карты бывают дебетовыми и кредитными.
									</p>

									<p>
										<b>Банковская карта</b> — это пластиковая карта, которая привязана к одному или нескольким счетам её владельца и позволяет осуществлять различные банковские операции.
									</p>

									<p>
										<b>Дебетовая карта</b> — это банковская карта, позволяющая владельцу распоряжаться деньгами в пределах суммы, находящейся на его счёте.
									</p>

									<p>
										<b>Кредитная карта</b> — это банковская карта, позволяющая владельцу распоряжаться деньгами, взятыми в долг у банка на условиях платности и возвратности.
									</p>
								
									<p>
										Внешне такие карты ничем не отличаются. И те, и другие позволяют получать (снимать) деньги в банкомате и платить за товары в магазине. Разница между ними в том, что по дебетовой карте вы можете потратить только те деньги, что сами до этого внесли в банк (в быту говорят — «положили на карточку»).
									</p>
								
									<p>
										А вот по кредитной карте потратить можно и больше денег, чем находится на вашем счету. Банк автоматически — без дополнительных переговоров с вами, а лишь на основе заключённого договора, по которому вы такую карту получили, — предоставляет вам кредит, когда вы платите больше, чем ранее внесли в банк. Конечно, возможность такого «перерасхода за счёт банка» небезгранична. Заключая с вами договор о выдаче кредитной карты, банк обязательно оговаривает в этом документе несколько важных обстоятельств. На них следует обратить особое внимание, прежде чем этот договор подписывать:
											<li><b>Лимит овердрафта.</b> Эти загадочные слова обозначают предельную сумму, которую вы (скажем, в месяц) можете потратить сверх денег, имеющихся на вашем счету в этом банке;</li>
											<li><b>Льготный период (Grace Period).</b> Так банкиры называют число дней, на протяжении которых вы можете вернуть возникший у вас по кредитной карте долг без дополнительной платы, т.е. процентов за кредит (перерасходовал 20 тыс. р. — столько же банку и вернул, если успел это сделать вовремя);</li>
											<li><b>Условия получения права на льготный период.</b> Воспользоваться льготным периодом обычно можно только в том случае, если вы кредитной картой платили при покупке товаров или услуг. А если вы просто сняли деньги в банкомате, то банк вам льготный период не предоставит. Дело в том, что при оплате картой покупок часть этой платы магазин отдаёт банку. А когда вы снимаете деньги в банкомате, банк никакой выгоды от этого не получает и никаких льгот вам соответ- ственно не даст;</li>
											<li><b>Процент по кредитному овердрафту.</b> Это то, сколько процентов от перерасходованной суммы надо будет ежемесячно платить банку, если вы не успели вернуть деньги на протяжении льготного периода. Понятно, что придётся погашать и сам долг ежемесячным внесением определённой суммы (обычно она составляет порядка 10% суммы долга);</li>
											<li><b>Годовой тариф за обслуживание кредитной карты.</b> Так называется плата, которую банк будет с вас взимать просто за то, что вы взяли у него кредитную карту (по дебетовой карте плата, впрочем, тоже есть). Обычно плата за обслуживание кредитных карт российских банков не превышает 1000 р. в год;</li>
											<li><b>Штрафные санкции.</b> Так в договоре будут названы наказания, которые банк сможет на вас наложить за нарушение условий пользования кредитной картой. Например, банк может просто потребовать с вас уплаты дополнительной денежной суммы — штрафа. Далее он может в наказание установить более высокую процентную плату за кредит. И наконец, может внести плохую запись в вашу кредитную историю, что сильно затруднит вам получение любых других кредитов в будущем.</li>
									</p>
								
									<p>
										<i>Кредитной историей во всём мире принято называть ваше личное «Дело о кредитовании». Проще говоря, это сборник сведений о том, где, у какого банка, какую сумму вы брали в долг и как потом этот долг возвращали (погашали).</i> Хранятся кредитные истории в электронных базах данных специальных организаций — кредитных бюро.
									</p>
								
									<p>
										Завершая разговор о банковской карте, стоит сказать, что вместе с ней вам дадут запечатанный конверт. В него будет вложен договор о карте. При получеии банковской карты, вам нужно будет ввести PIN-код по которому банкоматы и терминалы приёма платежей будут понимать, что вы законный владелец данной карты и можете распоряжаться деньгами, имеющимися на ней. Свой PIN-код надо хранить обязательно отдельно от карты и никому не показывать, чтобы вашими деньгами не мог распорядиться какой-нибудь мошенник.
									</p>
								
									<img src="assets/images/system_bank.png" width="350" height="300">
									<img src="assets/images/system_bank2.png" width="350" height="300">

									<p>
										Деньги, украденные с вашей карты, можно попробовать вернуть через банк. Но дело это пока в России крайне непростое и долгое. Поэтому в работе с картой лучше соблюдать предельную осторожность: например, не платить ею в ресторанах или кафе, где карту уносят из зала и могут сделать с неё копию для последующей кражи денег. Лучше снять заранее деньги с карты в банкомате и заплатить официанту наличными. Кстати, снимая с карты деньги в банкомате, нужно тоже проявлять осторожность. Некоторые мошенники ухитряются устанавливать на банкоматы специальные накладки — <b>скиммеры</b>, которые позволяют записать все данные твоей карты и её PIN-код, когда вы снимаете деньги в таком «усовершенствованном» банкомате. И тогда всего за несколько дней от денег на вашем карточном счёте не останется и следа.
									</p>
								
									<h3>Как понять, что банкомат опасен</h3>

									<p>
										<b>Накладки на картоприемник.</b> Если на банкомате установлен скиммер, то карта сначала проходит через специальный считыватель, а потом попадает в обычный картоприемник. Посмотрите, нет ли такой накладки на щели, в которую вставляется карта:
									</p>
									<img src="assets/images/skimmer.png" width="400" height="400">
									<p>
										Если на банкомате стоит антискимминговая накладка (полупрозрачная штука из пластика), попробуйте качнуть или повернуть ее. Настоящая будет намертво прикреплена к банкомату. Поддельная люфтит, отходит и шатается. Обычно мошенники монтируют шпионские устройства на несколько часов, потому что боятся проверок инспекторов. Поэтому они используют простые способы крепления: скотч, клей, честное слово. Зачастую мошенники оставляют следы: щели, клеевые подтеки и сколы. Лучше не использовать банкомат, картоприемник которого выглядит так, будто кто-то ковырял его отверткой или облил клеем.
									</p>
								
									<p>
										<b>Фальшпанели и черные точки.</b> Мошенники делают поддельные панели, монтируют в них видеокамеры, а потом незаметно крепят к банкомату: на диспенсер для денег, под козырек или под экран. Есть случаи, когда камеру прятали в стенде для рекламных брошюр того же банка:
									</p>
									<img src="assets/images/skimming_02.png" width="400" height="400">
									<p>
										Не стесняйтесь изучить поверхность банкомата. Потрогайте панели. Обычно фальшивые держатся плохо. Если что-то шатается, изучите деталь поближе. Плохой сигнал — если на ровной поверхности встретилось миниатюрное углубление, которое издалека выглядит как черная точка. Присмотритесь: возможно, это глазок видеокамеры.
									</p>
								
									<p>
										<b>Выпуклая или отличающаяся по тону клавиатура.</b> У мошенников не всегда есть возможность покрасить фальшивые запчасти в цвет банкомата. Несовпадение по цвету и тону легко заметить. Чаще всего лжеклавиатуру садят на клей или двусторонний скотч. Поэтому при наборе клавиш ощущается небольшой люфт. Накладка отходит от банкомата:
									</p>
									<img src="assets/images/skimming3.png" width="400" height="400">
									<p>
										Если клавиатура отличается по фактуре, выпирает или шатается, попробуйте поддеть ее ногтем. Если банкомат атакован мошенниками, под накладкой вы увидите настоящую клавиатуру. Если банкомат старый, со сколами и затертыми панелями, а клавиатура выглядит как новая — это тоже плохой знак. Не бывает, чтобы банки меняли клавиатуру отдельно от корпуса банкомата.
									</p>
								
									<blockquote class="situation">
										<div class="info_text">
											<h4>Отвечаем на вопрос: что же такое кредитные карты, как ими пользоваться и чем дебетовые карты отличаются от кредитных?</h4>
