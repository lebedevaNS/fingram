                                                Истина, скорее, лежит посередине между этими крайними точками зрения. FOREX — это не пирамида, а реальный механизм биржевой торговли валютой. Но надеяться на заработок — и тем более регулярный заработок — на нём могут только крупные игроки: банки и финансовые компании. У них есть прямой выход на биржу, а самой игрой на колебаниях курсов валют заняты профессионалы. Обычный же человек «с улицы» шансов на успех здесь не имеет. Все его деньги с высокой вероятностью будут потеряны, а точнее, станут доходом компаний-дилеров, предлагающих ему обслуживание на таком рынке. Вот они выиграют точно, поскольку плату с игрока возьмут в любом случае: выиграл он или проиграл. Беда в том, что многие люди втягиваются в FOREX как в азартную игру, пытаясь отыграться и для этого влезая в долги или продавая имущество, пока не становятся нищими. Они забывают очень верную старинную русскую пословицу «Не за то отец сына бил, что играл, а за то, что отыгрывался». Иными словами, сказки про гарантированный доход на FOREX — приманка для доверчивых и финансово неграмотных людей, наивно полагающих, что они со своими 500 долл. могут выиграть у банков, которые оперируют сотнями миллионов и на которые трудятся профессиональные финансисты и математики.
