                                            </blockquote><br>

                                            <p>
                                                Сам термин «курс валюты» достаточно красноречив. Строго говоря, он означает цену денежной единицы одной страны, выраженную в денежных единицах другой страны. Изначально, в старину, это делалось, исходя из весовых пропорций содержания золота или серебра в подлинных монетах разных стран. Но сегодня никто не говорит «цена доллара» или «пропорция обмена доллара». А цены валют — пропорции их обмена — меняются столь часто, что вместо термина «цена» прижился термин «курс». Он происходит от латинского слова <i>cursus</i>, означающего «бег, движение». А рынок, на котором осуществляются сделки купли-продажи иностранных валют, называется <b>валютным</b>.
                                            </p>

                                            <p>
                                                Интерес к курсам валют в России совсем не случаен. Тому есть несколько серьёзных причин. <b>Причина первая</b>. Россия — страна, живущая продажей за рубеж топлива, минералов и металлов. Минфин ожидает, что в 2023–2025 годах зависимость бюджета от нефтегазовых доходов начнет снижаться, их доля сократится на 10%. В 2022 году нефтегазовые доходы должны обеспечить 40,9% доходов российского бюджета. Об этом говорится в проекте «Основных направлений бюджетной, налоговой и таможенно-тарифной политики на 2023 год и плановый период 2024 и 2025 годов» (документ есть у РБК, его подлинность подтвердил источник, близкий к ведомству). Но надо понимать следующую особенность российской экономики. Мы продаём наши природные ресурсы за рубеж за доллары, а платят налоги государству добывающие компании рублями. И если курс рубля по отношению к доллару снизится, то одна и та же долларовая выручка нефтяных и газовых компаний выразится в большей сумме рублей. И тогда государственный бюджет, получив в виде налогов с этих компаний большую сумму в рублях, сможет больше потратить на различные нужды страны.
                                            </p>

                                            <p>
                                                <b>Причина вторая</b>. Россия покупает за границей очень много товаров, без которых страна не может жить. В 2013 г. почти половина (44%) товаров, которые продавались в российских магазинах, были ввезены из-за рубежа. А доля зарубежных лекарств в российских аптеках и больницах и того выше — более 70%. Поэтому снижение курса рубля по отношению к иностранным валютам сразу оборачивается повышением цен и на продукты питания, и на лекарства. Иными словами, одна и та же цена в долларах выразится в большем количестве рублей, которое придётся заплатить за килограмм мяса или упаковку лекарства. Растут при этом расходы не только граждан, но и государства.
