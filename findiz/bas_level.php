                                                        <i>
                                                            Представим, что вы только что окончили университет и начали устраиваться на работу. Родители пообещали вам купить автомобиль, когда вы устроитесь на работу, а бабушка — передать в дар принадлежащие ей земельный участок в соседней деревне и однокомнатную квартиру на окраине города.
                                                        </i>
                                                    </p>
                                                    <h4>Какие суммы вам придётся уплатить в качестве налогов как физическому лицу в связи с получением дохода и приобретением имущества?</h4>
                                                </div>
                                            </blockquote><br>

                                            <p>
                                                Мы не будем здесь касаться того, какие налоги платят в России коммерческие организации. В любой такой организации есть специалист по учёту затрат и результатов деятельности компании — бухгалтер. И он в вопросах налогообложения компаний разбирается профессионально, а значит, будет вам в этом помощником. Поговорим о подоходном налоге (об уже упоминавшемся выше НДФЛ). Величину его определят в той организации, где вы будете работать, в процентах от вашего заработка по единой для всех (независимо от размера дохода) ставке 13%.
                                            </p>

                                            <p>
                                                Причём берётся этот налог не только с заработной платы, но и с доходов от сдачи имущества (например, квартиры) внаём, а также доходов от операций на финансовом рынке (кроме доходов по вкладам в банках, если они были там размещены под обычные, а не под особенно высокие проценты).
                                            </p>
                                        
                                            <p>
                                                Также налог следует заплатить, если вы выиграли в лотерею или получили подарок. Для граждан, проживающих в Российской Федерации более 183 дней в году, такие доходы облагаются по налоговой ставке 13%. В отношении граждан, получивших рассматриваемые доходы и находившихся в Российской Федерации менее 183 дней в году, применяется налоговая ставка в размере 30%. При получении дохода в виде выигрыша от участия в лотерее вашим родителям необходимо заполнить налоговую декларацию по форме 3-НДФЛ.
                                            </p>
                                        
                                            <p>
                                                Налог на доходы, полученные в виде подарков от российских организаций и индивидуальных предпринимателей, исчисляется, удерживается и перечисляется дарителем, т. е. в большинстве случаев физическим лицам нет необходимости декларировать такие доходы самостоятельно. Доходы в виде подарков не облагаются налогом на доходы физических лиц в случае, если их стоимость не превышает 4 тыс. р. в год.
                                            </p>
                                        
                                            <p>
                                                Если вы или ваши родители получили подарки от физических лиц, не являющихся индивидуальными предпринимателями, то получатель подарка сам должен задекларировать доход и уплатить налог. Такая обязанность возникает в случае получения подарков в виде: недвижимого имущества, транспортных средств, акций, долей, паёв.
                                            </p>
                                        
                                            <p>
                                                С пенсий и социальных пособий (например, по болезни, беременности или инвалидности) налоги не взимаются.
                                            </p>
                                        
                                            <p>
                                                Ещё один налог, который платят многие россияне, — это налог на имущество. Таким налогом облагается имущество раз в год, а рассчитывается его величина в процентах от инвентаризационной стоимости имущества, которым человек владеет: квартиры, дома, дачи, садового участка или гаража. Налог исчисляется на основании данных об инвентаризационной стоимости по состоянию на 1 января каждого года.
                                            </p>
                                        
                                            <p>
                                                Если у вас есть автомобиль или мотоцикл, то государство возьмёт с вас налог на транспортное средство. Величина этого налога различается по регионам России, мощности двигателя и вида транспортного средства.
                                            </p>
                                        
                                            <p>
                                                Есть ещё один налог, который нередко уплачивают граждане, — земельный. Плательщиками такого налога будут лица, владеющие земельными участками. Налог платится по месту нахождения земельного участка. Более подробно о том, как платить земельный налог, можно узнать в налоговой инспекции, к которой относится этот участок. Ставки налога в зависимости от региона различаются. Например, в Кемеровской области он установлен в размере 0,3% кадастровой стоимости участка. Кадастровая стоимость участка — это стоимость земельного участка, которая определяется государством с учётом классификации земли и её назначения.
                                            </p>
                                        
                                            <p>
                                                Налог на доходы физических лиц, налог на имущество, транспортный, земельный налог являются <b>прямыми налогами</b>, они взимаются с доходов и имущества конкретного человека, а их размер зависит от размера получаемого дохода и стоимости имущества.
                                            </p>
                                        
                                            <p>
                                                Но есть ещё налоги, которые государство берёт с нас только в том случае, когда мы покупаем товары или потребляем услуги. Эти налоги нам невидимы, так как входят в цену товара. Их уплачивает продавец. Такие налоги называют <b>косвенными</b>. К ним относятся налог на добавленную стоимость, который входит в цену практически всех товаров и многих услуг (в РФ он составляет 18 %), акцизы, налагаемые на табачные, винноводочные, ювелирные изделия и некоторые другие товары, а также таможенные пошлины. Таможенные пошлины взимаются с провозимых из-за границы товаров. Отсюда следует, что, чем больше человек покупает товаров и потребляет услуг, тем больше он платит налогов.
                                            </p>
                                        
                                            <p>
                                                Завершая разговор о налогах, надо сказать, что людей, которые нарушают налоговые законы, государство может наказать, например, запретом выезда за рубеж (даже если человек едет срочно на лечение), а в особо серьёзных случаях — привлечь к уголовной ответственности и посадить в тюрьму.В соответствии с обновлённым законодательством налоговые санкции носят чаще всего финансовый характер. Они могут быть трёх видов: недоимка, штраф и пеня. Недоимкой называется сумма сокрытого или заниженного дохода. Нарушение в форме недоимки наказывается штрафом — дополнительным денежным взысканием. Другими словами, при наличии недоимки вас обязывают уплатить не только её, но и штраф. Пеня — это та сумма, которая взыскивается при задержке уплаты налогов. Пеней может облагаться и недоимка, и штраф, и правильно рассчитанный, но не уплаченный в срок налог. Вот почему все налоги надо платить в полном объёме и вовремя!
                                            </p>
                                        
                                            <blockquote class="situation">
                                                <div class="info_text">
                                                    <h4>Отвечаем на вопрос: какие суммы вам придётся уплатить в качестве налогов как физическому лицу в связи с получением дохода и приобретением имущества?</h4>
