                                                Кредиты можно классифицировать по различным видам и формам. Формы кредита тесно связаны с его структурой и в определённой степени с сущностью кредитных отношений. Структура кредита включает такие основные элементы, как кредитор, заёмщик и стоимость, которую кредитор передаёт во временное пользование заёмщику (так называемую ссуженную стоимость).
                                            </p>
                                            
                                            <img src="assets/images/credit.png" width="500" height="400">

                                            <p>
                                                Так, по характеру передаваемой во временное пользование стоимости кредит,  делится на три формы:
                                                <li>
                                                    <b>Товарная.</b>  Представляет собой такой кредит, при котором в долг выдаётся товар, а по истечении определённого периода возвращается товар стоимостью выше. Данная форма кредита на сегодняшний день характерна в основном для стран с низким уровнем развития кредитных отношений.
                                                </li>
                                                <li>
                                                    <b>Денежная.</b>  Самая распространённая на сегодняшний день форма кредита, которая представляет собой передачу денежных средств кредитором заёмщику на условиях платности, возвратности и срочности.
                                                </li>
                                                <li>
                                                    <b>Смешанная.</b>  Кредит выдаётся товаром, а его погашение происходит в денежном выражении, которое превышает приблизительную стоимость товара.
                                                </li>
                                            </p>

                                            <p>
                                                По основным группам заёмщиков выделяют следующие формы кредитов:
                                                <li>
                                                    Физическим лицам (гражданам);
                                                </li>
                                                <li>
                                                    Юридическим лицам (компаниям, организациям).
                                                </li>
                                            </p>

                                            <p>
                                                По целям использования заёмных средств кредит делится на:
                                                <li>
                                                    Целевой;
                                                </li>
                                                <li>
                                                    Общего характера (нецелевой).
                                                </li>
                                            </p>
                                        
                                            <p>
                                                Кредиты могут предоставляться в национальной или иностранной валюте. По признаку платности кредит может быть процентным или беспроцентным.
                                            </p>

                                            <p>
                                                По типу кредитора выделяют следующие формы кредитов:
                                                <li>Банковский, в том числе межбанковский;</li>
                                                <li>Коммерческий;</li>
                                                <li>Государственный;</li>
                                                <li>Международный (иностранный).</li>
                                            </p>
                                
                                            <p>
                                                <b>Коммерческий кредит</b> означает, что кредитором является не кредитная организация, кредит предоставляется в ходе торговой сделки, поэтому его называют ещё и торговым. Кредит может предоставить любой субъект, временно имеющий в своём распоряжении свободные денежные средства.
                                            </p>

                                            <p>
                                                <b>Государственный кредит</b> — это форма займов государства у населения, юридических лиц или иностранных государств, которые совершаются для того, чтоб покрыть дефицит госбюджета или финансирования государственных расходов. С помощью государственного кредита государство активизирует дополнительные финансовые ресурсы и может финансировать общегосударственные расходы, то есть лучше выполнять свои функции.
                                            </p>

                                            <p>
                                                <b>Международный кредит</b> — это предоставление ссуд в коммерческой или банковской форме кредиторами одной страны заёмщикам другой страны.
                                            </p>

                                            <p>
                                                <b>Ипотечный кредит</b> — это предоставление денежных средств на условиях, характерных для кредита, под залог недвижимого имущества.
                                            </p>

                                            <p>
                                                <b>Ломбардный кредит</b> — краткосрочный кредит в денежной форме под залог легкореализуемого движимого имущества (как правило, ювелирных украшений).
                                            </p>

                                            <p>
                                                Автокредит является целевой ссудой. Цель предоставления средств определена сразу — это покупка автомобиля, и потратить деньги на другие цели у клиента не получится, поскольку перечисление средств на его банковский счёт, а тем более предоставление кредита наличными, не предполагается. При автокредите банки принимают в залог приобретаемое авто.
                                            </p>

                                            <p>
                                                По способу погашения выделяют:
                                                <li>Кредиты, погашаемые единовременным взносом (платежом)</li>
                                                <li>Кредиты, погашаемые частями (долями) в течение всего срока действия кредитного договора.</li>
                                                <li>Аннуитетное погашение — это погашение долга равными долями.</li>
                                                <li>Дифференцированное погашение — это погашение платежами, понижающимися от остатка</li>
                                            </p>
