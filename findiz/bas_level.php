                                            <p>
                                                На фондовом рынке, как и на любом другом, действуют законы спроса и предложения: чем выше спрос на товар — тем выше цена товара. В случае с фондовым рынком и товаром, продающимся на нём — ценными бумагами, — взаимосвязь величины спроса, предложения и цены выглядит следующим образом:
                                                <li>чем выше спрос на ценную бумагу, тем выше её стоимость;</li>
                                                <li> чем выше предложение ценной бумаги, тем ниже её стоимость.</li>
                                            </p>
                                            
                                            <p>
                                                В изменении стоимости ценной бумаги кроется её потенциальная привлекательность для инвестора (человека или организации, которые имеют средства и хотят их защитить от инфляции, а ещё лучше — приумножить).
                                            </p>

                                            <p>
                                                Стоимость ценной бумаги принято называть её котировкой. На котировку ценной бумаги влияют её потенциальная доходность. Чем выше потенциальная доходность, тем выше спрос, тем выше стоимость ценной бумаги. Спрос на ценные бумаги формирует тот, кто хочет купить ценную бумагу. Предложение ценных бумаг формирует тот, кто хочет продать ценную бумагу.
                                            </p>

