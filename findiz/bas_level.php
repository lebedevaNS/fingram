                                                <b>Причина четвёртая</b>. За последнее время наши соотечественники всё больше и больше посещают разные страны мира. Поток увеличивается на всех направлениях: европейском, азиатском, латиноамериканском. Для простых граждан, собирающихся в путешествие, очень важным будет то, как меняется курс денежной единицы государства, в которое они отправляются путешествовать. Ведь при серьёзном увеличении курса валюты того государства, куда запланирована поездка, её цена может увеличиться значительно, что в свою очередь может привести даже к отказу от такого путешествия. Например, в конце 2013 г. (на 10 декабря) тур в Грецию на две недели на двух человек стоил 1800 евро, т. е. 80 838 р., а в марте 2014 г. (на 22 марта) — 90 324 р. Это только цена самой путёвки, а ведь ещё нужно взять деньги с собой!

