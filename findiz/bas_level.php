                                            <p>
                                                Во-первых, стоит последовать совету известного российского предпринимателя Александра Галицкого и составить для себя самый простой бизнес-план. <i><b>Бизнес-план</b> — это документ, в котором описываются все основные аспекты предпринимательской деятельности, анализируются главные проблемы, с которыми может столкнуться предприниматель, и определяются основные способы решения этих проблем.</i> Не стоит думать, что бизнес-план необходим только крупным предприятиям, организующим своё дело с размахом. Как показывает мировая практика, он нужен для всех форм и видов предпринимательства, и прежде всего для того, чтобы тщательно проанализировать свои идеи, проверить их разумность, реалистичность и уменьшить тем самым риск неудачи. Кроме того, бизнес-план необходим для представления тем, у кого предприниматель собирается занять деньги или иное имущество на реализацию проекта, для подтверждения реалистичности задуманного дела и способности возвратить кредит, арендованное имущество.
                                            </p>
                                        
                                            <img src="assets/images/business_plan.png" width="280" height="230" style="float:right; margin-left:20px;">
                                            <h3 style="margin-top: 50px;">Содержание бизнес-плана</h3>
                                            <ol>
                                                <li>Что сделает ваш бизнес интригующим для потребителей, партнёров и для вас лично? (Видение)</li>
                                                <li>Почему ваш бизнес будет своевременным и нужным кому-то именно сейчас и почему именно вы должны его создать? (Фундамент твоего бизнеса)</li>
                                                <li>Какого именно успеха, измеряемого во времени объёмом продаж, прибылью и количеством потребителей, вы хотите достичь? (Цели)</li>
                                                <li>А те ли ребята в вашей команде и на своих ли они местах?</li>
                                                <li>Что вы собираетесь продавать и как эти продукты будут соответствовать ожиданиям потребителей и рынка? (Продукт)</li>
                                                <li>Кто уже есть на рынке с похожим товаром или услугой и чем вы будете от них отличаться? (Конкуренция)</li>
                                                <li>Каким способом вы собираетесь рассказать будущим покупателям о своих преимуществах? (Маркетинг)</li>
                                                <li>Как вы собираетесь оплачивать расходы по запуску и первоначальному ведению своего бизнеса и какой доход вы можете пообещать тем, кто даст вам денег в расчёте на 1 р. их вложений? (Финансы)</li>
                                                <li>Как вы собираетесь определять, что ваш бизнес идёт по правильному пути? И как часто будете такую проверку проводить? (Измерение прогресса)</li>
                                                <li>Как всё в природе, бизнес имеет свою жизнь. Что вы хотите сделать со своим бизнесом в будущем: оставить детям в наследство, продать или превратить в открытую акционерную компанию?</li>
                                            </ol>
                                        
                                            <p>
                                                Теперь представим, что вы смогли придумать действительно разумный бизнес и у него есть неплохие — по вашему мнению — шансы на успех. Но остаётся совсем не праздный вопрос: где взять деньги, чтобы этот проект реализовать?
                                            </p>
                                        
                                            <p>
                                                У начинающего предпринимателя редко имеются для этого собственные сбережения. А потому обычновозникает идея эти деньги одолжить. Такой способ привлечения дeнег называется <b>долговым финансированием</b>. Им, в принципе, можно воспользоваться, но стоит иметь в виду следующее:
                                                <li>Если брать деньги у друзей и родственников, то можно навсегда испортить с ними отношения из-за разных взглядов на развитие молодого бизнеса или из-за его краха;</li>
                                                <li>Можно попытаться найти богатых людей, готовых вложиться в стартапы ради дохода более высокого, чем в банках по депозитам. Но они могут вмешиваться в управление бизнесом, что вам вряд ли понравится, и даже отстранить вас от управления созданным тобой бизнесом вообще </li>
                                                <li>Одолженные деньги надо вернуть строго в срок, в полной сумме и с процентами, а для молодой фирмы зачастую это просто нереально;</li>
                                                <li>Стоимость ставки по таким кредитам обычно выше, чем по кредитам давно существующим фирмам, из-за большей премии за риск, которую хотят получить кредиторы («Если уж мы рискнём и дадим вам деньги под новый бизнес с неясной перспективой, то хотим за это процент повыше, чем можем заработать, кредитуя уже устоявшийся бизнес. А иначе зачем нам рисковать?»);</li>
                                                <li>Обычно банкиры требуют в качестве обеспечения возвратности кредита залог личного имущества (например, квартиры), что создаёт для семьи начинающего предпринимателя очень опасную жизненную ситуацию;</li>
                                                <li>Фирме, начинающей свой путь с нуля (стартапу), получить деньги в кредит у банка в России крайне трудно.</li>
                                            </p>
                                        
                                            <p>
                                                Чтобы вам дали кредит или как-то помогли в начале вашего дела, необходимо представить грамотный финансовый план. В этом случае вы будете оперировать понятиями «издержки», «доход», «прибыль». Что это такое?
                                            </p>
                                        
                                            <p>
                                                <i><b>Издержки</b> — это выраженные в денежной форме затраты на факторы производства, необходимые для создания экономических благ.</i> Издержки бывают <i>постоянные, т. е. не зависящие от объёмов выпускаемой продукции</i> (например, аренда помещения), и <i>переменные, находящиеся в прямой зависимости от объёма выпускаемой продукции</i> (сырьё, материалы, из которых создаётся продукт).
                                            </p>

                                            <p>
                                                <i><b>Доход</b> — это денежные суммы или материальные ценности, которые были получены в течение определённого периода и в результате осуществления какой-либо деятельности.</i> 
                                            </p>

                                            <p>
                                                <i><b>Прибыль</b> — положительная разница между суммарными доходами и затратами на производство или приобретение, хранение, транспортировку, сбыт этих товаров и услуг. Прибыль = Выручка - Затраты. В случае, если результат отрицателен, его называют <b>убытком</b>.</i>
                                            </p>

                                            <p>
                                                B нашей стране есть ещё один вариант получения средств на открытие предприятия — взять кредит не у банка, а у центра поддержки малого бизнеса. Это государственные организации, которые зачастую получают крупные банковские кредиты и самостоятельно могут кредитовать начинающих предпринимателей. Подобные организации присутствуют практически в каждом городе России. Кандидатов на финансирование они отбирают самостоятельно, но на основании жёстких критериев:
                                                <li>Hеобходим чёткий и грамотный бизнес-план (куда более обстоятельный, чем мы описали выше);</li>
                                                <li>Bсе документы на создание новой компании;</li>
                                                <li>Oбоснование прибыльности будущего проекта (сколько копеек прибыли будет получать новый бизнес на рубль вложенных в него средств). </li>

                                            </p>
                                        
