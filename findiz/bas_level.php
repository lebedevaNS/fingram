                                            <h3>Этапы создания собственного бизнеса</h3>

                                            <p>
                                                Создание собственного бизнеса связано с реализацией следующих этапов:
                                                <li>Выдвижение бизнес-идей;</li>
                                                <li>Анализ состояния рынка и отбор лучшей бизнес-идеи;</li>
                                                <li>Разработка бизнес-плана и поиск источников финансирования;</li>
                                                <li>Выбор организационно-правовой формы ведения бизнеса;</li>
                                                <li>Государственная регистрация предпринимательской деятельности;</li>
                                                <li>Постановка на учёт в налоговой инспекции;</li>
                                                <li>Получение лицензий (в случае необходимости).</li>
                                            </p>

                                            <p>
                                                Вопрос выбора бизнес-идеи является одним из самых сложных. Существует множество советов и подходов, которые позволяют решить, какая из бизнес-идей лучше. Но все они сходятся в том, что лучшей будет та бизнес-идея, для реализации которой потребуются меньшие первоначальные вложения, и которая принесёт в будущем больший доход.
                                            </p>

                                            <p>
                                                Поэтому для того, чтобы выбрать бизнес-идею, необходимо cравнить сегменты рынка и определить их привлекательность по следующим	параметрам:
                                                    <ol>
                                                            <li> ёмкость сегмента (размер сегмента) — оценка потенциального дохода, который может получить предприниматель;</li>
                                                            <li>потенциал роста сегмента — оценка динамики изменения дохода, который может получить предприниматель;</li>
                                                            <li> уровень конкуренции в сегменте.</li>
                                                    </ol>
                                            </p>

                                            <p>
                                                <i><b>Потенциальная ёмкость рынка</b> — это количество продукта, которое потенциально может быть реализовано на рынке. Количество продукта может быть измерено в натуральном выражении (в количестве проданных за определённый период на определённой территории товаров), но чаще ёмкость рынка рассчитывают в стоимостном выражении (в деньгах) как произведение количества товаров на их цену.</i>
                                            </p>

                                            <p>
                                                <i>
                                                    <b>Фактическая ёмкость (объём) рынка</b> — это количество продукта, фактически реализованного всеми производителями на рынке. Доступная ёмкость рынка — это количество продукта, который может реализовать отдельная фирма.
                                                </i>
                                            </p>

                                            <h3>Бизнес по франшизе</h3>

                                            <p>
                                                Открыть собственный бизнес можно, воспользовавшись бизнес-схемой под названием <i><b>франчайзинг</b> — крупная фирма, владеющая собственным брендом, предоставляет партнёру франшизу, то есть право работать под уже известной на рынке торговой маркой.</i> Наибольшее распространение эта форма бизнеса получила в сферах общественного питания, магазинов обуви и одежды, аптек и т.п.
                                            </p>

                                            <p>
                                                Работа по франшизе для предпринимателя предоставляет следующие преимущества:
                                                <li>возможность начать собственный бизнес с минимальными начальными вложениями;</li>
                                                <li>получение помощи при открытии собственного бизнеса со стороны крупного партнёра;</li>
                                                <li> получение вместе с торговой маркой репутации и известности среди потребителей;</li>
                                                <li>возможность использовать результаты широкомасштабных рекламных компаний, проводимых владельцем торговой марки;</li>
                                                <li> возможность использования результатов научных разработок, проводимых владельцем торговой марки;</li>
                                                <li>получение гарантии постоянного снабжения;</li>
                                                <li> возможность приобретения на выгодных условиях оборудования (основныхфондов) у владельца торговой марки.</li>
                                            </p>

                                            <p>
                                                Работа по франшизе для предпринимателя имеет следующие недостатки:
                                                <li>меньшая самостоятельность при ведении бизнеса;</li>
                                                <li>уменьшение дохода за счёт обязательных выплат владельцу торговой марки;</li>
                                                <li>потеря репутации владельца торговой марки приведёт и к потере репутации бизнеса, открытого предпринимателем.</li>
                                            </p>

                                            <p>
                                                Но за право работать по франшизе необходимо заплатить. Условия предоставления франшизы разные и закрепляются Договором коммерческой концессии. Как правило, покупатель франшизы выплачивает первоначальный взнос <b>(паушальный взнос)</b> и делает периодические выплаты <b>(роялти)</b>. Размер паушального взноса может быть очень разным: от руб. до млн долларов. Периодичность выплаты роялти также может различаться: каждый месяц, каждый квартал, каждые полгода, один раз в год.
