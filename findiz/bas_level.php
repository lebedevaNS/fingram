										Слово «бизнес» заимствовано в английском языке и в большинстве словарей с этимологией английское business по своим истокам связывают с busy — деятельный, занятой. Среднеанглийское busi, древнеанглийское bisig несли значения «занятый, поглощённый, прилежный, усердный». Немецкое слово с родственными корнями — beziehen со значениями «покупать, закупать, заказывать», что в конечном счёте и ведёт нас к спектру современных значений понятия «бизнес». Бизнес понимается узко как предпринимательская деятельность, направленная на получение прибыли. Например, торговля, коммерческая деятельность. Бизнес понимается институционально как клиентура, покупатели, публика, учреждения, специализирующиеся в определённой сфере предпринимательства.
									</p>
								
									<img src="assets/images/business.png" width="300" height="250" style="float:left; margin-right:20px;">
									<p>
										Скажем так — чтобы вырастить свою фирму до крупных размеров и стать очень богатым человеком, действительно нужны предпринимательский талант и очень сильный характер. Но создание небольшой фирмы, приносящей вполне приличный доход, по плечу очень многим людям. Многим молодым предпринимателям для успеха просто не хватает сил и выдержки, ведь в первые годы им предстоит работать очень много. Исследования показывают, что рабочая неделя при самозанятости составляет в среднем 64 ч, т. е. более 10 ч в день. А так работать готовы не все. Вот наиболее типичные ошибки начинающих предприни- мателей:
										<li>Hепонимание рынка, на который выходит фирма. Её основатель даже не пробует провести анализ рынка: сколько человек на нём готовы купить предлагаемый товар или услугу, чего они ждут от такого товара на самом деле, как и где им удобно покупать, сколько конкурентов будут мешать продвижению товара и т. п.;</li>
										<li>Oтсутствие регулярного и квалифицированного планирования бизнеса. Начинающие предприниматели редко задумываются о том, что и когда им надлежит сделать в будущем, чтобы фирма не прогорела, из чего будут формироваться доходы, какие предстоят расходы по фирме и пр.;</li>
										<li>Hедостаток управленческих навыков. Начинающий предприниматель обычно никогда и никем не управлял и совершенно не представляет, как организовать работу будущих подчинённых во вновь созданной фирме;</li>
										<li>Hедостаток денежных средств  для  развития  бизнеса. У начинающего предпринимателя не хватает собственных и заёмных средств, чтобы запустить свой проект или продержать его «на плаву», пока выручка не станет регулярно превышать текущие затраты.</li>
									</p>
								
									<p>
										Вот что пишет об этом американский консультант по бизнесу Дэн Шоубел, директор компании Millennial Branding: <i>«Многие начинающие предприниматели считают бизнес чем-то лёгким и занимательным — и их проекты умирают спустя год. Многие проекты, созданные молодыми вдохновленными предпринимателями, никогда не взлетят из-за недостатка ресурсов, денег и хороших наставников. Большинство молодых профессионалов не бизнесмены, но они пытаются ими стать, так как думают, что это “круто”. Они воспринимают бизнес как альтернативу, если не могут найти нормальную работу. Безработица и экономические проблемы многих из нас заставили очнуться и пересмотреть свое отношение к карьерным достижениям. Реальность сурова: большинство мелких “бизнесов” не переживают первых двух лет, а большинство людей не предприниматели».</i>
