                                            <h3>Участники фондового рынка и их задачи</h3>

                                            <p>
                                                В операциях с ценными бумагами всегда присутствуют три участника, роли которых чётко определены: эмитент, инвестор и фондовый рынок.
                                            </p>

                                            <p>
                                                <i>
                                                    <b>Эмитент</b> — это государство или организация, выпускающая ценные бумаги.
                                                </i>
                                            </p>

                                            <p>
                                                <i>
                                                    <b>Инвестор</b>  — это физическое лицо или организация, покупающая ценные бумаги.

                                                </i>
                                            </p>

                                            <p>
                                                <b>Фондовый рынок</b> — это рынок, на котором встречаются эмитент и инвестор. Поскольку на фондовом рынке идёт торговля ценными бумагами, то с одной стороны есть тот, кто выпустил, то есть <b>эмитировал</b>, ценную бумагу (цель выпуска ценной бумаги — привлечение средств для развития компании), — это <b>эмитент</b>, а с другой стороны есть тот, кто имеет свободные средства и решил вложить эти средства не в банк, а в предприятие, рассчитывая, что такое вложение может принести больший доход по сравнению с размещением средств на депозите в банке, — это <b>инвестор</b>.
                                            </p>

                                            <p>
                                                Как и на любом рынке, на фондовом рынке встречаются продавец и покупатель, в качестве которых могут выступать и эмитент, и инвестор:
                                                <li> эмитент может продавать ценные бумаги для привлечения инвестиций и развития своего предприятия;</li>
                                                <li> эмитент может покупать ценные бумаги других компаний для того, чтобы разместить свободные средства и получить дополнительную прибыль, то есть, по сути, он становится инвестором для других предприятий;</li>
                                                <li>инвестор покупает ценные бумаги для того, чтобы разместить имеющиеся у него свободные средства, сохранить их от инфляции и приумножить передав предприятию, которое в будущем может получить прибыль и тем самым обеспечить доход инвестору.</li>
                                            </p>

                                            <p>
                                                <i>Физические лица — частные инвесторы, а юридические лица — институциональные инвесторы.</i>
                                            </p>

                                            <p>
                                                Фирма-организатор торгов ценными бумагами называется фондовой биржей. Доход этой компании формируется за счёт отчислений процента от сделок продавцами и покупателями. Фондовая биржа технически обеспечивает участников торгов помещением, оборудованием, компьютерной поддержкой и т. п.
