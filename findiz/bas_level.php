                                            <li>выбрать надёжный банк, которому можно доверить свои сбережения;</li>
                                            <li>выбрать форму хранения сбережений и срок, на который они будут помещены в банк</li>
                                            <li>проследить, чтобы вклад полностью подпадал под страховую защиту государства;</li>
                                            <li>внимательно ознакомиться со всеми документами, требующими вашей подписи, и не нарушать принятых на себя обязательств, чтобы избежать финансовых штрафов (это особенно важно при обращении в банк за кредитом).</li>
