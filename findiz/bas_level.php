                                            <p>
                                                <i>
                                                    <b>Ценная бумага</b> — это удостоверение права на имущество.
                                                </i>
                                            </p>

                                            <p>
                                                <i>Исторически первыми ценными бумагами являлись <b>векселя — долговые расписки</b></i>. Банкир выдавал деньги в долг и брал расписку, в которой должник писал, что была взята в долг такая-то сумма и он обязуется вернуть всю сумму и проценты к такому-то сроку. Позже расписки стали выпускать на особой бумаге с защитой от подделки, но на них по-прежнему указывалась сумма, выдаваемая в долг, процент, который необходимо вернуть сверх выданной суммы, и срок выплаты долга (срок погашения).
                                            </p>

                                            <p>
                                                <i>
                                                    <b>Вексель</b> — это простейший вид долговой ценной бумаги, по которой процент выплачивается держателю вместе с возвратом основного долга.
                                                </i>
                                            </p>

                                            <p>
                                                Другим видом ценных бумаг являются <b>облигации</b>. В средние века облигации (обязательства вернуть долг) выпускало государство, для того чтобы привлечь в казну деньги. Позже облигации начали выпускать промышленные предприятия, торговые компании, банки. Доход по облигации в виде процента, называемого купонами, в отличие от векселя, выплачивают отдельно от основного долга (частным случаем является бескупонная облигация, доход по которой формируется за счёт разницы между ценой покупкиоблигации и её номиналом).
                                            </p>

                                            <p>
                                                <i>
                                                    <b>Облигация</b> — это ценная бумага, закрепляющая право её владельца на возврат долга.
                                                </i>
                                            </p>

                                            <p>
                                                Акция, в отличие от облигации, не является обязательством вернуть долг, а подтверждает право владельца на часть (долю) собственности компании. Акции могут выпускать только компании.
                                            </p>

                                            <p>
                                                <i>
                                                    <b>Акция</b> — это ценная бумага, закрепляющая право владельца на часть собственности в предприятии.
                                                </i>
                                            </p>

                                            <p>
                                                Доход по акции, полученный от распределения прибыли, называется дивидендом. В отличие от облигации, процент по которым фиксирован и обязателен к выплате, выплата дивидендов по акции и их размер зависят от результатов работы компании за год, наличия прибыли, размера прибыли, решения собрания акционеров о том, направить прибыль на выплату дивидендов или инвестировать в компанию, и т. д. Владелец акции может получить доход от ценной бумаги двумя путями:
                                                <li>акция может принести дивиденды (выплаты части прибыли компании);</li>
                                                <li>акцию можно продать по более высокой цене, чем цена покупки (разница между ценой покупки и ценой продажи представляет собой доход владельца акции), то есть доход возможен за счёт роста котировок (цен) на акции.</li>
                                            </p>

                                            <p>
                                                <i>
                                                    Выпуск ценных бумаг называется <b>эмиссией</b>. Тот, кто выпустил ценную бумагу, называется <b>эмитентом ценной бумаги</b>. Эмиссия ценных бумаг позволяет эмитенту (банку, компании, государству) привлечь средства.
                                                </i>
                                            </p>

                                            <p>
                                                Выделяют две категории ценных бумаг:
                                                <li>долговые (например, вексель, облигация) — от слова «долг», поскольку ценная бумага является подтверждением того, что держатель (тот, кто приобрёл ценную бумагу) передал эмитенту (тому, кто выпустил ценную бумагу) денежные средства в долг, а эмитент обязуется вернуть долг вместе с процентами.</li>
                                                <li> долевые (например, акция) — от слова «доля», поскольку ценная бумага является подтверждением того, что эмитент (тот, кто выпустил ценную бумагу) передаёт держателю (тому, кто приобрёл ценную бумагу) права на долю (часть) своей компании, а значит, и на долю будущейвозможной прибыли.</li>

                                            </p>

                                            <p>
                                                Тот, кто приобрёл вексель, получил право требовать с должника (эмитента векселя) долг с процентами к определённому сроку. Тот, кто приобрёл облигацию, получил право требовать с должника (эмитента облигации) выплату дохода по облигации (выплату купонов) с указанной периодичностью и выплату основного долга по облигации. В отличие от векселя, основная сумма долга и сумма процентов по облигации выплачиваются не одновременно. Тот, кто приобрёл акцию, приобрёл право на долю в компании (долю в части прибыли компании), которая выпустила акции.
                                            </p>
