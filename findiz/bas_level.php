									</p>

									<p>
										<b>Малый бизнес</b> — это разновидность бизнеса, в котором бизнес-единицами являются малые предприятия (предприятия, имеющие ограничения по количеству сотрудников, а также юридические и налоговые особенности ведения бизнеса). Малые предприятия с юридической точки зрения могут быть созданы в форме индивидуального предпринимательства (ИП) или в форме юридического лица (например, общества с ограниченной ответственностью). Зарегистрировать ИП существенно легче, чем ООО, да и налоговая и финансовая отчётность куда проще. Но вместе с тем форма ИП имеет ряд недостатков. Например, индивидуальный предприниматель отвечает по своим обязательствам своим имуществом. Это означает, что в случае образования долга в связи с ведением хозяйственной деятельности долг может быть возмещён из имущества индивидуального предпринимателя. ИП не подходит для ведения совместного бизнеса, некоторые фирмы не хотят взаимодействовать с ИП, так как у многих нет расчётного счёта в банке (что разрешено по закону) и отсутствует печать. ИП не может осуществлять некоторые виды деятельности. ИП перечисляет фиксированную сумму страховых взносов, даже если не вёл активную хозяйственную деятельность. Несмотря на недостатки, в целом для начала ведения бизнеса можно использовать эту форму, многое зависит от того, чем вы собираетесь заниматься, причём в одиночку или совместно с друзьями.
