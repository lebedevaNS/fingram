                                                            Представим, что Сергей Федотов вложил сбережения в сумме 100 тыс. р. в доллары в январе 2003 г., заплатив за каждый доллар по 31,82 р. А в июле 2008 г., т.е. спустя пять с половиной лет, ему вдруг срочно понадобились рубли. Но к этому моменту курс доллара опустился до 23,35 р. за 1 долл.
                                                        </i>
                                                    </p>
                                                    <h4>Как курсы валют меняются и можно ли выиграть на разнице курсов во времени?</h4>
                                                </div>
                                            </blockquote><br>
                                        
                                            <p>
                                                Если в ситуации Сергея курс опустился до 23,35, то это значит, что в рублях Сергей получит сумму почти на 27% меньшую, чем когда-то потратил на покупку валюты. И за доллары, купленные тогда на 100 тыс. р., сейчас при продаже можно получить только 73 тыс. р. Иными словами, он не только не спас свои сбережения от обесценения, но и проиграл.
                                            </p>

                                            <p>
                                                Доллар от инфляционного обесценения деньги не спас, поскольку курс доллара за время хранения не только не вырос быстрее, чем цены, но даже снизился, что усугубило потери нашего незадачливого владельца валютных сбережений.
                                            </p>
                                        
                                            <p>
                                                А если бы наш владелец сбережений поступил поумнее и хранил свои валютные средства не в наличной форме, а в банке, открыв валютный депозит с процентным доходом? Помогло бы ему это защитить сбережения от обесценения? Давайте посчитаем, приняв процент по долларовому депозиту на уровне 6% (такую доходность обычно предлагали по подобным вкладам российские банки в 2003–2008 гг.). Тогда за пять лет хранения депозита в банке его владелец добился бы роста первоначальной суммы в 1,34 раза (1,065), а реальная покупательная способность его исходных сбережений составила бы 57 540,94 (42 941 р. х 1,34). Картина становится чуть лучше, чем при хранении наличной валюты дома, но принципиально не меняется — валюта сбережения от обесценения всё равно не спасла.

                                            </p>

                                            <p>
                                                <i>
                                                    Можно увеличить свои доходы, покупая и продавая валюты разных стран. Но это требует специального обучения, постоянного внимания к ситуации на валютном рынке и готовности к риску.
                                                </i>
                                            </p>

                                            <p>
                                                Несколько улучшают ситуацию так называемые <b>мультивалютные депозитные вклады</b>, основное назначение которых как раз и заключается в защите от валютных рисков во времена, когда курсы валют сильно колеблются.
                                            </p>

                                            <img src="assets/images/dollars.png" width="350" height="250" style="float: left; margin-right: 20px">
                                            <p>
                                                Открывая мультивалютный вклад, клиент банка получает возможность при возникновении каких-либо колебаний курса быстро перевести деньги из одной валюты в другую. Денежные средства конвертируются безналичным путём в рамках вклада без ограничения по числу раз и без потери доходности. Иными словами, мультивалютные вклады позволяют клиентам не только получить гарантированное вознаграждение в виде процентов по вкладу, но и извлечь дополнительную прибыль за счёт изменения курсов валют.
                                            </p>

                                            <p>
                                                Тем не менее, чтобы заработать на мультивалютном депозите что-то, кроме гарантированной ставки по вкладу, нужно постоянно следить за курсами валют на бирже и уметь прогнозировать ситуацию на валютном рынке. Неискушённому же вкладчику, который такими умениями не обладает, мультивалютный депозит не принесёт ничего, кроме лишних комиссий за конвертацию денежных средств, которые могут существенно снизить доход от процентных ставок.
                                            </p>

                                            <p>
                                                Как правило, отечественные банки предлагают размещать средства примерно поровну в трёх валютах: долларах, евро и рублях. Однако в последнее время всё большее внимание российских вкладчиков привлекают менее распространённые в России валюты, такие как фунт стерлингов, швейцарский франк, японская иена и даже китайский юань, хотя найти банк, предлагающий открыть мультивалютный вклад в этих валютах, намного сложнее.
                                            </p>

                                            <p>
                                                Наличие мультивалютного счёта помогает решить ещё одну проблему, с которой сталкиваются россияне, любящие ездить на отдых за границу, а именно как выгоднее купить валюту в дорогу.
                                            </p>

                                            <p>
                                                Oбычно те, кому необходимо «превратить» доллары в евро или наоборот, меняют «ненужную» валюту на рубли, а после уже за рубли покупают «нужную». При такой двойной операции они дважды теряют на разнице обменных курсов, что в общей сложности составляет от 5 до 10% обмениваемой суммы. Между тем в большинстве банков запросто можно обменять доллары на евро (или наоборот) напрямую, минуя пересчёт в рубли. Такая операция называется <b>«обменом по кросс-курсу</b>» и позволяет почти вдвое сократить издержки.
                                            </p>
                                        
                                            <blockquote class="situation">
                                                <div class="info_text">
                                                    <h4>Отвечаем на вопрос: как курсы валют меняются и можно ли выиграть на разнице курсов во времени?</h4>
                                                    <p>
