                                            <h3>Доход по векселю</h3>

                                            <p>
                                                Доход по векселю представляет собой разность между суммой, полученной при погашении, и ценой покупки. Доход зависит от цены приобретения векселя: номинальной цены векселя или цены с дисконтом.
                                            </p>
                                            
                                            <h4>Пример:</h4>

                                            <p>
                                                <li>
                                                    Tы приобрёл вексель номиналом рублей, с процентной ставкой 5% годовых и сроком погашения до февраля года по цене номинала (то есть за 10000 рублей). Если ты не продал ценную бумагу раньше, то февраля года ты сможешь получить назад затраченную сумму — и процент — рублей. Доход составит: 10000 руб. + 500 руб. - 10000 руб. = 500 руб.
                                                </li>
                                            </p>
                                        
                                            <p>
                                                Если владелец векселя держит его до даты погашения, то его доход равен проценту по векселю.
                                            </p>

                                            <p>
                                                <li>
                                                Ты приобрёл вексель номиналом рублей, с процентной ставкой 5% годовых и сроком погашения до февраля года по цене номинала (то есть за 10000 рублей). Если июня года тебе срочно понадобятся деньги, то ты можешь продать этот вексель. Из-за срочности ты готов продать вексель за рублей — по цене ниже номинала — с дисконтом. Для нового владельца векселя, который приобрёл его с дисконтом, доход от погашения векселя февраля года составит: 10000 руб. + 500 руб. - 9000 руб. = 1500 руб.
                                                </li>
                                            </p>

                                            <h3>Доход по облигации</h3>

                                            <p>
                                                Доход по облигации складывается из:
                                                <li> периодически выплачиваемого процента по купону;</li>
                                                <li>разницы между ценой покупки и ценой погашения облигации эмитентом</li>
                                            </p>

                                            <p>
                                                Частным случаем является бескупонная облигация, доход по которой формируется только за счёт разницы между ценой покупки облигации и ценой погашения облигации эмитентом. В том случае, если процент по купону не выплачивался, доход по одной облигации определяется только как разность между ценой продажи и ценой покупки облигации. Продажа облигации может принести как прибыль, так и убыток:
                                                <li> если облигация была приобретена по цене ниже номинала (с дисконтом) и погашена эмитентом (тем, кто выпустил облигацию) по цене номинала, то образуется прибыль;</li>
                                                <li>если облигация была приобретена по цене выше номинала (с премией) и погашена эмитентом (тем, кто выпустил облигацию) по цене номинала, то образуется убыток.</li>
                                            </p>

                                            <h3>Доход по акции</h3>

                                            <p>
                                                Доход по акции складывается из:
                                                <li> дивиденда, который может быть выплачен по акции;</li>
                                                <li> изменения рыночной цены акции.</li>
                                            </p>
                                        
                                            <p>
                                                Для того чтобы получать дивиденды, необходимо быть владельцем акции. Для того чтобы получить доход от изменения курса акции, необходимо её продать. В том случае, если дивиденды не выплачивались, доход по одной акции определяется только как разность между ценой её продажи и ценой покупки. Продажа акции может принести как прибыль, так и убыток:
                                                <li>если продать акцию по цене выше цены покупки, то образуется прибыль;</li>
                                                <li> если продать акцию по цене ниже цены покупки, то образуется убыток.</li>
                                            </p>

                                            <h3>Доходность ценной бумаги</h3>

                                            <p>
                                                Доходность ценной бумаги определяется как отношение дохода, полученного от ценной бумаги, к затратам на её приобретение и выражается в процентах:<br>
                                                <img src="assets/images/formula.png" width="300" height="100">
                                                <br>
                                                Д — доходность, %;<br>
                                                Ск.п. — цена ценной бумаги на конец периода;<br>
                                                Сн.п. — цена ценной бумаги на начало периода.
                                            </p>
                                        
                                            <h3>Портфель ценных бумаг</h3>

                                            <img src="assets/images/portfel.png" width="530" height="360">

                                            <p>
                                                Портфель ценных бумаг может быть сформирован для достижения одной из двух целей:
                                                <li> получения дохода (за счёт роста цены на ценные бумаги, входящие в портфель);</li>
                                                <li> минимизации рисков (за счёт приобретения надёжных, но низкодоходных ценных бумаг).</li>
                                            </p>
                                        
                                            <p>
                                                Поэтому при формировании портфеля ценных бумаг выбирают одну из наиболее приоритетных для инвестора целей:
                                                <ol>
                                                    <li>Cформировать портфель ценных бумаг с низким риском, но и низкой доходностью — высоконадёжный, но низкодоходный портфель — такой портфель формирует консервативный инвестор.</li>
                                                    <li>Сформировать портфель ценных бумаг с высоким риском, но и высокой доходностью — высокодоходный, но рискованный портфель — такой портфель формирует агрессивный инвестор.</li>
                                                    <li> Сформировать портфель, сочетающий высокодоходные, но рискованные ценные бумаги с надёжными, но низкодоходными ценными бумагами, — диверсифицированный портфель — такой портфель формирует умеренный инвестор.</li>
                                                </ol>

                                            </p>

                                            <p>
                                                <i>
                                                Соотношение конкретных видов ценных бумаг в портфеле называется <b>структурой портфеля ценных бумаг.</b>
                                                </i>
                                            </p>
