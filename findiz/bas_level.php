                                                        Однажды ваш младший брат увидел по телевизору рекламу Пенсионного фонда России и задал неожиданный вопрос: «А что такое пенсионный фонд? Что такое пенсия и зачем она нужна?». Вы ответили, что до пенсии ещё очень далеко и думать об этом сейчас глупо. Ваши родители с этим не согласились, но объяснить всё толком сами не смогли.

                                                        </i>
                                                    </p>
                                                    <h4>Как появилась пенсия и что вообще это такое?</h4>
                                                </div>
                                            </blockquote><br>
                        
                                            <p>
                                                Историки считают, что первыми настоящими пенсионерами были римские легионеры. Безбедную старость им обеспечивал передаваемый во владение надел земли, захваченной Римом в результате войн. И в России первыми пенсионерами тоже были военные. В указе Петра I «О пенсионе бывшим военным» говорилось: «Назначить достойное пожизненное содержание, дабы не позорили честь мундира». Пенсии были исключительной привилегией офицерства. Крепостных рекрутов, отслуживших 25 лет, отправляли в родные деревни.
                                            </p>
                                        
                                            <p>
                                                Историю  современной  пенсионной системы принято отсчитывать с конца XIX в., когда канцлер Германии Отто фон Бисмарк официально ввёл в 1889 г. солидарную государственную пенсию для всех работающих. 20 лет спустя эстафета была подхвачена Великобританией и Австралией, а Соединённые Штаты Америки пришли к государственной пенсионной системе в 30-е гг. прошлого века. В СССР Закон о пенсиях был принят в 1956 г.
                                            </p>
                                        
                                            <p>
                                                <i>
                                                    <b>Пенсия</b> — это регулярное денежное пособие, выплачиваемое лицам, имеющим инвалидность, либо достигшим пенсионного возраста, либо потерявшим кормильца.
                                                </i>
                                            </p>
                                        
                                            <p>
                                                <i>
                                                    <b>Пенсионная система</b> — это совокупность организационных форм, норм и правил, в которых реализуется пенсионное обеспечение граждан государства.
                                                </i>
                                            </p>
                                        
                                            <p>
                                                <i>
                                                    <b>Пенсионный фонд РФ</b> — это крупнейшая организация России по оказанию социально значимых государственных услуг гражданам. ПФР создан для государственного управления средствами пенсионной системы и обеспечения прав граждан РФ на пенсионное обеспечение.
                                                </i>
                                            </p>
                                        
                                            <p>
                                                В чём же причина трудностей с пенсиями, которые переживают сейчас многие развитые страны, в том числе Россия?
                                            </p>
                                        
                                            <p>
                                                Причина первая — рост продолжительности жизни, а значит, и количества лет, которые человек проживает после выхода на пенсию по старости и в течение которых ему эту пенсию надо платить. Создатели пенсионных систем никак не предполагали, что люди станут жить так долго.
                                            </p>
                                        
                                            <p>
                                                Причина вторая — сокращение количества детей в семьях. Если сто лет назад в Европе и России часто встречались семьи с пятью и более детьми, то в начале нынешнего века обычным явлением стали семьи с одним ребёнком.
                                            </p>
                                        
                                            <p>
                                                Причина третья — <b>принцип «солидарности поколений»</b>, некогда положенный в основу выплаты пенсий, не позволяет рeшить проблемы обеспечения старости в современных условиях.
                                            </p>
                                        
                                            <p>
                                                Этот принцип означает, что определённая часть заработков молодых и работающих людей государством изымается и направляется в «общую кассу» страны (пенсионный фонд), из которой затем выплачиваются пенсии пожилым и нетрудоспособным гражданам (пенсионерам). Другими словами, пенсии старикам формируются за счёт части заработков работающего поколения, как в своё время эти старики, будучи молодыми, обеспечивали пенсиями своих родителей.
                                            </p>
                                        
                                            <p>
                                                Эта система хорошо работает, когда в стране на одного пожилого, неработающего и живущего в старости недолго человека приходится несколько молодых и работающих. Во времена СССР на 1 пенсионера приходилось 2-3 работника и собирать деньги на выплату пенсий удавалось. Но сейчас ситуация изменилась. По расчётам ученых, уже к 2040 г. в России пенсионеров (если правила выхода на пенсию не изменятся) будет столько же, сколько работающих, и денег на выплату пенсий просто не хватит. Их не хватает уже сегодня: «солидарные отчисления» молодых и работающих граждан меньше того, что нужно для выплаты даже тех небольших пенсий, которые получают сегодня пожилые люди в нашей стране. Дефицит Пенсионного фонда составляет 3 трлн р., и государству приходится добавлять «на пенсии» из других своих доходов.
                                            </p>
                                        
                                            <p>
                                                Что это означает лично для вас? Что, когда вы подойдёте к старости, возможности её денежного обеспечения наверняка изменятся по сравнению с теми, которые существуют сейчас. И надежд, что государство сможет платить тебе пенсию по старости, позволяющую достойно существовать в пожилом возрасте, питать не следует.
                                            </p>
                                        
                                            <p>
                                                Вот почему умный и финансово грамотный человек должен заранее (лет после 30) начинать готовить себе «денежный механизм счастливой старости».
                                            </p>
                                        
