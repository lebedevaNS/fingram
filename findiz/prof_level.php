                                                <li>Доход за год не превысил 60 млн руб.</li>
                                            </p>

                                            <p>
                                                Индивидуальный предприниматель уплачивает 6% от размера потенциального дохода, который возможен для получения от ведения определённого вида деятельности на данной территории. Расчёт налога производится по формуле:
