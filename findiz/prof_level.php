                                            <img src="assets/images/actpass.png" width="450" height="400">
                                            <p>
                                                В общем виде под <b>активами</b> понимается всё то, в чём можно хранить богатство. Так богатство человека можно сохранить: в золоте, драгоценных камнях, товарах, недвижимости, ценных бумагах, наличных деньгах. Аналогично богатству человека богатство компании можно накопить и сохранить в материальных ценностях, ценных бумагах, оборудовании, запасах, в нематериальном виде (например, в стоимости бренда компании).
                                            </p>

                                            <p>
                                                В бухгалтерском балансе предприятия к активам относятся:
                                                <li>доходные вложения в материальные ценности;</li>
                                                <li>долгосрочные и краткосрочные финансовые вложения;</li>
                                                <li>основные средства;</li>
                                                <li>нематериальные активы;</li>
                                                <li>запасы;</li>
                                                <li>дебиторская задолженность.</li>
                                            </p>

                                            <p>
                                                Термин <b>«пассивы» </b>означает обязательства или источники. Пассивы показывают, из каких источников были сформированы активы предприятия.
                                            </p>

                                            <p>
                                                В бухгалтерском балансе предприятия к пассивам относят:
