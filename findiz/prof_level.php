                                            <h3>Неоптимальный выбор системы налогообложения</h3>

                                            <p>
                                                Прежде чем выбрать систему налогообложения, необходимо провести тщательные расчёты. Ещё одна из наиболее типичных ошибок предпринимателей связана с использованием упрощённой системы налогообложения (УСН), когда в качестве базы начисления налога используются «доходы минус расходы». Необходимо учитывать, что не всякие расходы учитываются при УСН (вычитаются из доходов).
                                            </p>

                                            <p>
                                                В качестве примера приведём несколько видов расходов, которые не учитываются при УСН:
                                                <li>Расходы на аренду, если она оформлена по незарегистрированному договору аренды;</li>
                                                <li>Расходы на строительство;</li>
                                                <li>Расходы на реконструкцию;</li>
                                                <li>Расходы на лицензирование;</li>
                                                <li>Расходы на страхование;</li>
                                                <li>Расходы на ведение бухучёта в полном объёме.</li>
                                            </p>

                                            <h3>Юридические ошибки начинающих предпринимателей</h3>

                                            <p>
                                                Значительная часть типичных ошибок начинающих предпринимателей связана с незнанием правовых норм. Начинающие предприниматели достаточно часто допускают ошибки, связанные с нарушением лицензионного законодательства, трудового законодательства, законодательства о рекламе. Штрафы для юридических лиц, незаконно работающих без лицензии, могут составить от 40000 до 50000 рублей.
                                            </p>

                                            <p>
                                                Для того, чтобы не получить штраф, необходимо ознакомиться с видами деятельности, подлежащими лицензированию. К ним относятся производство лекарственных средств, авиационной техники, оружия, обеспечение пожарной безопасности, услуги связи, образования, медицины и многие другие.
                                            </p>

                                            <p>
                                                Кроме того, ошибки могут связаны с незнанием законодательства о рекламе. Штрафные санкции могут быть наложены за несоблюдение закона «О защите детей от информации, причиняющей вред их здоровью и развитию». Кроме того, при создании рекламы необходимо учитывать ограничения на создание текстового и визуального контента. Предприниматель должен знать, что создание визуального контента связано с ограничениями на использование фотографий или рисунков (нельзя допускать нарушения закона «О персональных данных»). При создании текстов необходимо учитывать, что все слова, утверждения в рекламе должны быть подтверждены документально, чтобы не вводить в заблуждение покупателя.
                                            </p>

                                            <p>
                                                Для того, чтобы не нарушать законодательство о рекламе, следует соблюдать следующие правила:
                                                <li>не использовать в рекламе иностранные слова без перевода;</li>
                                                <li>не проводить рассылку sms-рекламы без предварительного согласия абонента;</li>
                                                <li>не допускать некорректного сравнения с продукцией других предпринимателей;</li>
                                                <li>не использовать в рекламе торговые марки других производителей (без права на их использование);</li>
                                                <li>не допускать неэтичных образов в рекламе.</li>
                                            </p>

                                            <p>
                                                Нарушения, связанные с незнанием трудового законодательства, могут быть чреваты для предпринимателя штрафами в размере до 100000 руб.
                                            </p>

                                            <p>
                                                Наиболее распространёнными нарушениями, которые допускают предприниматели, нанимающие на работу сотрудников, являются:
                                                <li>Заключение трудового договора не в письменной форме;</li>
                                                <li>Заключение гражданско-правового договора вместо трудового договора;</li>
                                                <li>Выплата заработной платы работникам один раз в месяц;</li>
                                                <li>Задержка выплаты заработной платы;</li>
                                                <li>Предоставление отпуска меньшей продолжительностью, чем установлено законодательством.</li>
