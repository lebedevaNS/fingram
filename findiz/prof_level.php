                                            </p>
                                            
                                            <p>
                                                <i>Торговля ценными бумагами связана с обеспечением технического процесса проведения торговли, обеспечения процедуры передачи прав собственности по продаваемым ценным бумагам и т. д. Решение этих вопросов обеспечивает специализированная организация — <b>фондовая биржа.</b> Фондовая биржа обеспечивает место проведения операций по купле-продаже ценных бумаг. Брокер обеспечивает выход на биржу для своих клиентов.</i>
                                            </p>

                                            <p>
                                                <i>
                                                    <b>Брокер</b> — это участник биржевой торговли, который совершает операции от своего имени, но за счёт своих клиентов (клиенты передают средства брокеру, а брокер принимает решения по управлению полученными средствами).

                                                </i>
                                            </p>

                                            <p>
                                                <i>
                                                    <b>Дилер</b> — это участник биржевой торговли, который совершает операции от своего имени и за свой счёт.
                                                </i>
                                            </p>

                                            <p>
                                                Оформление операций по купле-продаже ценных бумаг осуществляют специализированные организации: клиринговая палата, регистратор, депозитарий.
                                            </p>

                                            <p>
                                                <i>
                                                Клиринговая палата обеспечивает расчёты между клиентами и осуществляет взаимозачёт обязательств. Клиринговая палата — это профессиональный участник рынка ценных бумаг, который проверяет наличие встречных обязательств клиентов друг к другу и проводит их взаимозачёт. Регистратор ведёт учёт реестра владельцев ценных бумаг. Депозитарий хранит сертификаты ценных бумаг, регистрирует куплю-продажу ценных бумаг.
                                                </i>
                                            </p>

                                            <p>
                                                Соблюдение правил биржевой торговли контролирует Центральный банк. Служба Банка России по финансовым рынкам осуществляет лицензирование профессиональных участников рынка ценных бумаг, регистрацию эмиссийценных бумаг, контроль и надзор за деятельностью участников рынка.
                                            </p>

                                            <p>
                                                Все расчётные операции между клиентами являются платными, но клиринговая палата позволяет исключить взаимные переводы средств от одного клиента другому. Клиринговая палата сначала осуществляет расчёт и только затем — реальный перевод средств. Из двух клиентов, совершающих взаимные торговые операции, средства будут переведены тому, у кого разница между приходом и расходом будет положительной. У того клиента, у которого разница между приходом и расходом окажется отрицательной, будут списаны средства со счёта.
                                            </p>
