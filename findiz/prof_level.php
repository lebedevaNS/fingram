                                            <p>
                                                Количество компаний, на основе которых рассчитывается индекс, указывается в виде числа после буквенного обозначения индекса. Фондовый индекс отражает динамику акций ведущих компаний, которые в него входят.
                                            </p>

                                            <p>
                                                В мире используется свыше биржевых индексов: в России — биржевой индекс ММВБ (рассчитывается в рублях), биржевой индекс РТС (рассчитывается в долларах), а в США — Dow Jones, S&P 500 и др.
                                            </p>

                                            <p>
                                                Фондовый индекс всегда показывает динамику рынка в целом, но при этом каждая акция ведёт себя по-своему. <i><b>Биржевой, или фондовый, индекс</b> — это сводный индекс, вычисляемый на основе цен определённой группы ценных бумаг — «индексной корзины».</i>
                                            </p>

                                            <p>
                                                Рост биржевых индексов свидетельствует о том, что акции компаний, на основе которых он рассчитывается, пользуются спросом, то есть инвесторы делают оптимистичный прогноз о дальнейшем росте курса акций. Биржевые индексы являются базой для оценки портфеля ценных бумаг инвестора, который сравнивает показатели своего портфеля с биржевым индексом.
