                                            <p>
                                                Существуют различные методы и подходы к оценке справедливой, то есть незаниженной и незавышенной стоимости компании. Их выбор обусловлен целью, которая стоит перед компанией. Такими целями, во имя реализации которых производится оценка бизнеса, могут являться:
                                                <li>необходимость привлечения инвестиций для реализации бизнес-проектов;</li>
                                                <li>проверка экономической целесообразности инвестиционных вложений;</li>
                                                <li>эффективность использования производственно-технологического потенциала.</li>
                                            </p>
                                        
                                            <p>
                                                Необходимость в оценке справедливой (незавышенной и незаниженной) стоимости компании (в оценке стоимости бизнеса) возникает как для владельцев, так и для инвесторов. Владельцу необходимо оценить справедливую стоимость своей компании в следующих случаях:
                                                <li>Eсли владелец принимает решение продать компанию.</li>
                                                <li>Если владелец обращается в банк за кредитом для развития своей компании. Банку для принятия решения о выдаче кредита или отказа в его выдаче понадобится оценка стоимости компании.</li>
                                                <li>Если владелец обращается к инвесторам (частным инвесторам, фондам и т. д.) для привлечения инвестиций в развитие бизнеса.</li>
                                            </p>

                                            <p>
                                                Банкам необходима оценка стоимости компании для принятия решения о выдаче кредита. Инвесторам необходима оценка стоимости компании для принятия решения о вложении средств в компанию и для выбора наиболее выгодного проекта для инвестирования.
                                            </p>

                                            <p>
                                                <i>
                                                Для оценки стоимости предприятия могут использоваться несколько подходов: рыночный, затратный, доходный и сравнительный.
                                                </i>
                                            </p>

                                            <h3>Рыночный</h3>

                                            <p>
                                                Если акции компании котируются на фондовом рынке, то для того, чтобы провести оценку рыночной стоимости компании, необходимо умножить рыночную стоимость простых акций на их количество. Недостатком этого метода является его неприменимость к большинству отечественных компаний, чьи акции на рынке не представлены.
                                            </p>

                                            <h3>Затратный</h3>

                                            <p>
                                                Затратный метод оценки стоимости предприятия основан на принципе: стоимость оцениваемого предприятия равна сумме, которую нужно затратить на создание аналогичного предприятия.
                                            </p>

                                            <p>
                                                Этот метод основан на результатах финансово-хозяйственной деятельности компании, отражённых в статьях бухгалтерского баланса. Преимущество данной методики состоит в том, что её применение довольно просто, не требует привлечения экспертов. Этот метод оценки стоимости предприятия основывается на реально существующих активах.
                                            </p>

                                            <h3>Доходный</h3>

                                            <p>
                                                Доходный метод оценки стоимости предприятия основан на принципе: рациональный инвестор не отдаст за приобретаемый им бизнес больше, чем размер дохода (денежный поток, прибыль, другие формы материальной и нематериальной выгоды), который принесёт ему данный бизнес в будущем.
                                            </p>

                                            <p>
                                                Доходный метод позволяет сопоставить текущие затраты инвестора (владельцев компании) с будущими доходами с учётом времени поступления доходов и факторов риска, характерных для бизнеса, который нужно проанализировать. Доходный метод основан на учёте рисков инвестирования и оценке будущих доходов. Этот метод подходит для предприятий, находящихся на стадии роста или стабильного экономического развития.
                                            </p>

                                            <h3>Сравнительный</h3>

                                            <p>
                                                Суть этого метода состоит в определении стоимости путём сравнения с предприятием аналогом, имеющим рыночную стоимость. Наиболее вероятной величиной стоимости оцениваемого бизнеса может быть реальная цена продажи аналогичной фирмы, зафиксированная рынком.
                                            </p>

                                            <p>
                                                Недостатком такого метода является то, что крайне трудно найти абсолютно аналогичное предприятие: с таким же продуктом, уровнем известности на рынке, качеством обслуживания, уровнем квалификации персонала и т. п.
                                            </p>

                                            <p>
                                                Можно выделить три условия, влияющие на повышение стоимости компании:
                                                <li>Чем выше прибыль компании, тем выше её стоимость.</li>
                                                <li>Чем раньше компания начала получать прибыль, тем выше её стоимость.</li>
                                                <li> Чем ниже риск банкротства компании, тем выше её стоимость.</li>
                                            </p>
