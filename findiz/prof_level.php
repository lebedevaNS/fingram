                                            <p>
                                                Для того, чтобы начать заниматься бизнесом, необходимо найти источник его финансирования. Существует два основных источника: это собственные средства и привлечённые.
                                            </p>
                                            
                                            <p>
                                                <i>
                                                    <b>Собственные средства </b>— это средства, которые принадлежат тебе, поэтому за их использование не придется отчитываться.
                                                </i>
                                            </p>

                                            <p>
                                                <i>Личное имущество индивидуального предпринимателя является его собственными средствами и составляет основу его бизнеса.</i>
                                            </p>

                                            <p>
                                                К собственным средствам хозяйственного общества относятся уставный капитал, который вносится его учредителями при создании, и чистая прибыль компании. Развивать бизнес за счёт собственных средств возможно, но всё, безусловно, зависит от их объёма. Собственные средства снизят некоторые риски по ведению бизнеса. Однако стоит отметить, что предприниматели, которые используют и свои собственные средства, и привлечённые, более успешны.
                                            </p>

                                            <h4>Так откуда же можно получить денежные средства на развитие и ведение бизнеса?</h4>

                                            <p>
                                                Самый распространённый источник — это кредит банка. В настоящее время банки предлагают кредиты на открытие и развитие бизнеса, пополнение оборотных средств, рефинансирование ранее выданных кредитов. Условия по кредитам разные: сроком до 10 лет, процентная ставка от 11.6% годовых, суммы до 200 млн. рублей
                                            </p>

                                            <p>
                                                Ещё один распространённый вариант кредитования малого бизнеса — это лизинг. Под лизингом понимается сделка по приобретению дорогостоящего имущества за счёт банка (лизингодателя), который будет являться собственником имущества до момента полной оплаты его стоимости клиентом (лизингополучателем). После выполнения всех условий договора имущество переходит в собственность клиента. 
                                            </p>

                                            <p>
                                                Также к источникам финансирования относят субсидии от государства. Если бы такое явление как субсидия имело лишь позитивную сторону, всё больше и больше предпринимателей обращалось бы к государству вместо того, чтобы искать инвесторов и брать кредиты в банке. Минусом является то, что вместо простого оформления бизнеса и начала работы необходимо будет пройти длинную цепочку согласований работников центра занятости (предварительно необходимо зарегистрироваться в центре занятости и подать пакет документов). Тем не менее, есть и плюс: полученные деньги не придется возвращать, если ты действительно будешь по назначению расходовать их на свой бизнес.
                                            </p>

                                            <p>
                                                При нехватке денежных средств для реализации бизнеса можно попытаться найти инвестора, который согласится вложить деньги в твоё дело. Это непростой процесс, который требует настойчивости, терпения и уверенности в собственных силах. Средства, привлекаемые на ведение бизнеса, расходуются на покупку основных и оборотных средств.
                                            </p>

                                            <p>
                                                <i>
                                                    <b>Основные средства</b> — это имущество фирмы, приобретённое для производства и использующееся в течение длительного времени. К основным средствам относится помещение, находящееся в собственности у компании, компьютерное оборудование, программное обеспечение. С части основных средств уплачивается налог на имущество.
                                                </i>
                                            </p>

                                            <p>
                                                <i>
                                                    <b>Оборотные средства</b> — это имущество фирмы, приобретаемое для производства нового продукта, услуги. К оборотным активам относят сырье, готовую продукцию, остатки денежных средств на расчётном счету компании либо в кассе.
                                                </i>
                                            </p>

                                            <p>
                                                <i>
                                                    <b>Расходы</b> — это траты, произведённые в процессе коммерческой деятельности.
                                                </i>
                                            </p>

                                            <p>
                                                <i>
                                                    <b>Доход</b> — денежные средства, полученные от предпринимательской деятельности за определённый период времени.
                                                </i>
                                            </p>

                                            <p>
                                                <i>
                                                    <b>Прибыль</b> — это разница между доходами и затратами (расходами) на производство или приобретение и сбыт этих товаров и услуг. Прибыль рассчитывается с помощью следующей формулы: Прибыль = Доход – Расход (в денежном выражении).
                                                </i>
                                            </p>

                                            <p>
                                                <i>
                                                    <b>Выручка</b> — это денежные средства, полученные (вырученные) фирмой, индивидуальным предпринимателем от продажи товаров, выполнения работ или оказания услуг. То есть это вся денежная сумма, которая получилась после реализации товара, выполнения работ, оказания услуг.
                                                </i>
                                            </p>

                                            <p>
                                                <i>
                                                    <b>Себестоимость</b> — это затраты (издержки) на производство продукции, выполнение работ или оказание услуг. К себестоимости относят денежные средства, которые были потрачены на закупку материалов, товаров (если ведется торговая деятельность), услуги, оказанные компании в процессе осуществления предпринимательской деятельности, заработная плата сотрудников, социальные взносы и прочие платежи.
                                                </i>
                                            </p>

                                            <p>
                                                <i>
                                                    <b>Чистая прибыль</b> — прибыль, которую получает фирма после уплаты всех предусмотренных законодательством налогов.
                                                </i>
                                            </p>

                                            <p>
                                                Когда сумма выручки больше, чем расходы, это означает, что бизнес чувствует себя хорошо, потому что есть достаточное количество денег, чтобы оплатить все расходы бизнеса. Кроме того, это показатель хорошего управления бизнесом.
                                            </p>

                                            <p>
                                                Когда сумма расходов больше, чем полученный доход, это означает, что бизнес работает плохо, и денег, полученных при продаже товаров и/ или услуг не хватает, чтобы оплатить все расходы, необходимые для существования бизнеса.
                                            </p>
                                        
                                            <p>
                                                Бизнес рентабелен, когда сумма дохода за определённый период превышает сумму всех затрат в этом периоде. Когда сумма всех затрат превышает сумму дохода, то бизнес является нерентабельным, и речь идёт об убыточном бизнесе.
                                            </p>
