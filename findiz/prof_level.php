                                            <h3>Портфель частного инвестора</h3>

                                            <p>
                                                Инструментами биржевой торговли являются ценные бумаги. В соответствии с российским законодательством <i><b>ценная бумага</b> — это документ или запись на специальном счёте, удостоверяющая имущественные и иные права её собственника (держателя) по отношению к лицу, выпустившему ценную бумагу.</i>
                                            </p>
                                            
                                            <p>
                                                Ценные бумаги, с которыми проводятся операции на бирже, можно в самом общем виде разделить на три основные категории: облигации, акции и инструменты, дающие право на другой инструмент (производные).
                                            </p>

                                            <p>
                                                Облигация — это долговая ценная бумага, по которой эмитент — компания или государство — обязуется выплатить инвестору определённую сумму и определённый процент в будущем.
                                            </p>

                                            <p>
                                                Акция — эмиссионная ценная бумага, предоставляющая её владельцу право на участие в управлении акционерным обществом и право на получение части прибыли в форме дивидендов.
                                            </p>

                                            <p>
                                                В общей сложности доступно около инструментов биржевой торговли, и выбрать наиболее подходящий не так уж и просто. Свой портфель инвестора гражданин наполняет различными инструментами, то есть ценными бумагами, чтобы снизить риски потерь собственного капитала. Стандартный портфель частного инвестора комплектуется различными инструментами, обычно в него входят от до таких инструментов. Наполняя свой портфель рискованными инструментами, частный инвестор может потерять весь свой капитал, но имеет шанс получить крупный выигрыш.

                                            </p>

                                            <h3>Биржевые риски</h3>
                                            <img src="assets/images/risk.png" width="560" height="400">
                                            
                                            <p>
                                                Биржевые риски — неотъемлемые атрибуты любой биржевой жизни. Основные виды рисков:
                                                <li>Oтраслевые — такие риски связаны с цикличным изменениями в отраслях, вследствие чего стоимость акций компаний может увеличиваться или уменьшаться.</li>
                                                <li>Политические — подразумевают собой нестабильность экономической ситуации, в следствие чего компании внутри государства могут прекратить свою деятельность.</li>
                                                <li>Инфляционные — последствием инфляции может стать обесценивание капитала инвестора.</li>
                                                <li>Селективные — связаны с неправильным выбором ценных бумаг при формировании портфеля.</li>
                                            </p>

                                            <p>
                                                Мы рассмотрели лишь некоторые виды рисков. На деле же их гораздо больше. Каждый участник торгов на бирже всегда пытается получить прибыль, попутно решая множество проблем, связанных с минимизацией рисков, предпринимая различные действия для того, чтобы избежать ситуаций, которые могут привести к потере крупных сумм. Биржевые риски имеют одну характерную особенность — им подвержены абсолютно все участники торгов. От убытков не застрахован никто, поэтому к ним всегда нужно быть готовым.
                                            </p>

                                            <p>
                                                Частный инвестор всегда должен помнить, что если игнорировать или недооценивать риски на бирже, то можно столкнуться с очень большими потерями, либо полностью лишиться своих средств. Биржевая торговля всегда связана с риском потерь. В зависимости от обстоятельств, которые могут сложиться на рынках, риски могут быть высокими или не очень.
                                            </p>

                                            <p>
                                                Всегда есть биржевые риски, которые контролировать не получается: это политические, экономические и риски регулирования. Считается, что все остальные риски (кредитный, процентный и операционный, расчётный и ликвидационный, юридический риски) контролировать возможно.
                                            </p>
