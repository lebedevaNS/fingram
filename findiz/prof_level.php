                                                <h3>ПИФ</h3>

                                                <p>
                                                    Ранее в лекциях мы рассматривали паевые инвестиционные фонды, рассмотри их подробнее. Паевой инвестиционный фонд, или ПИФ — своеобразная «корзина» ценных бумаг и других доходных активов, который разбит на множество мелких долей, т. е. паёв.
                                                </p>
                                                
                                                <p>
                                                    <i>Предполагается передача финансов в доверительное управление специальной организации (управляющей компании) и последующее получение прибыли, если работа партнёров окажется успешной.</i> Основная цель, с которой инвесторы идут в паевые инвестиционные фонды, извлечение прибыли.
                                                </p>

                                                <p>
                                                    Управляющая компания работает за процент от финансовых транзакций, осуществляемых при использовании капитала вкладчиков. Инвестирование в паевые фонды осуществляется посредством покупки определённых долей в портфеле инструментов ПИФа, при этом вкладчики остаются их собственниками, управляющая компания всего лишь осуществляет необходимые финансовые транзакции.
                                                </p>

                                                <p>
                                                    ПИФ можно сравнить с копилкой, в которую поместили деньги несколько человек.
                                                </p>

                                                <p>
                                                    Частный инвестор часто ограничен в средствах, и ему недоступны многие ценные бумаги. Объединив свои средства, инвесторы получают возможность:
                                                    <li>выйти на рынок ценных бумаг с минимальной суммой средств;</li>
                                                    <li>распределить риски при вложении в ценные бумаги.</li>
                                                </p>

                                                <h3>Пример 1</h3>

                                                <p>
                                                    Eсли частный инвестор имеет рублей свободных денежных средств, то для самостоятельной игры на фондовой бирже этого, во-первых, недостаточно, а во-вторых, нельзя собрать портфель из ценных бумаг с разным уровнем риска.
                                                </p>

                                                <p>
                                                    ПИФ — это альтернатива размещению средств на депозит в банке. По депозиту можно получить гарантированный, но невысокий процент. Покупая пай в ПИФе, можно получить значительно больший доход, чем по депозиту, но сохранность первоначально вложенной суммы не гарантируется.
                                                </p>

                                                <h3>Пример 2</h3>

                                                <p>
                                                    Частный инвестор купил пай в ПИФе за 20000 рублей. Управляющая компания разместила средства пайщиков в акции, цена которых через полгода упала на 50%. Стоимость пая частного инвестора снизилась с 20000 до 10000 рублей.
                                                </p>

                                                <p>
                                                    В отличие от депозита, пайщик несёт расходы:
                                                    <li>Пай приобретается по цене выше стоимости пая на 0.5 – 1.2% (наценка управляющей компании).</li>
                                                    <li>Плата за управление активами ПИФа составляет от 0.5% до 5% стоимости пая.</li>
                                                    <li>При реализации пая управляющая компания вычтет из его стоимости до 1% от рыночной стоимости.</li>
                                                </p>

                                                <p>
                                                    Доход от вложения средств в ПИФы образуется за счёт изменения курсовой стоимости ценных бумаг, в которые вкладывает средства управляющая компания, организовавшая ПИФ
                                                </p>
