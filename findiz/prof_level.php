                                            <h3>Открытые паевые инвестиционные фонды (ОПИФы)</h3>

                                            <p>
                                                Такие ПИФы продают и принимают к погашению паи в любые рабочие дни. Это самые распространённые типы ПИФов в настоящее время.
                                            </p>

                                            <p>
                                                <i>
                                                    К ОПИФам относится каждый второй фонд. Пайщикам намного спокойней осуществлять вложения в открытые ПИФы, потому что они в любое время могут вернуть вложенные средства.
                                                </i>
                                            </p>

                                            <p>
                                                Правда, за спокойствие приходится платить пониженной доходностью, ибо для обеспечения возможности изъятия средств в любое рабочее время средства фонда должны быть размещены в наиболее ликвидные, а следовательно, менее доходные инвестиционные инструменты. Уровень риска у ОПИФ ниже, чем у закрытых и интервальных.
                                            </p>

                                            <h3>Интервальные паевые инвестиционные фонды (ИПИФы)</h3>

                                            <p>
                                                <i>
                                                    ИПИФы реализуют паи и принимают их к погашению лишь несколько раз в год в установленные промежутки времени, обычно это интервалы по две недели 4 раза в год.
                                                </i>
                                            </p>

                                            <p>
                                                Таких фондов — приблизительно четверть от всех ПИФов. Большинство интервальных фондов — это ПИФы акций или смешанных инвестиций. Эти ПИФы могут вкладывать часть средств в более доходные, но менее ликвидные инструменты инвестирования, ибо известно, что пайщики могут приходить не каждый день, а только раз в квартал. Доходность интервальных паевых инвестиционных фондов обычно выше, чем открытых ПИФов.
                                            </p>

                                            <p>
                                                Сегодня паи отдельных интервальных ПИФов без затруднений торгуются на бирже, и их можно купить или продать в рабочие дни, не дожидаясь открытия интервала.
                                            </p>

                                            <h3>Закрытые ПИФы (ЗПИФы)</h3>

                                            <p>
                                                По большей части ЗПИФы являются фондами недвижимости. Существуют ЗПИФы прямых инвестиций, венчурных инвестиций и т. д. По размерам привлекаемых средств ЗПИФы занимают лидирующее положение. Это происходит из-за того, что минимальный порог вхождения обычно намного превышает порог для интервальных и открытых ПИФов.
                                            </p>

                                            <p>
                                                ЗПИФы создаются на установленный срок от 1 года до 15 лет.
                                            </p>

                                            <p>
                                                ЗПИФы заметно отличаются от открытых или интервальных ПИФов. Паи закрытых фондов можно приобрести исключительно при образовании фонда, а погасить — при завершении деятельности фонда. Число инвестиционных паёв ЗПИФа ограничено и повышается только лишь в случае добавочного выпуска паёв.
                                            </p>
