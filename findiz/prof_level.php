                                                    <li>Выбрать вид экономической деятельности, которым ты планируешь заниматься (код по ОКВЭД). Код необходим для ведения статистического учёта государством. Если в будущем не исключено расширение видов деятельности, а присвоенный код ОКВЭД не будет предусматривать их, то необходимо будет получить дополнительный.</li>
