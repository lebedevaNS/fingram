                                <a href="#openModal_proflecture4">Лекция №4<br>Организационно-правовые формы</a>
                                </div>
                                <div id="openModal_proflecture4" class="modal">
                                <div class="modal-dialog">
                                    <div class="modal-content">
                                        <div class="modal-header">
                                             <h3 class="modal-title" style="margin-left: 50px; font-size: 22px;"> Организационно-правовые формы</h3>
                                            <form action="prof_level.php" target="_self">
                                                <button class="close">×</button>
                                            </form>
                                        </div>
                                        <div class="modal-body">
                                            <p>
                                                <i>
                                                    <b>Организационно-правовая форма хозяйствующего субъекта</b> — это признаваемая законодательством той или иной страны форма ведения предпринимательской деятельности, фиксирующая способ закрепления и использования имущества, механизмы контроля со стороны государства.
                                                </i>
                                            </p>
                                        
                                            <p>
                                                Организационно–правовая форма:
                                                <ol>
                                                    <li>определяет и закрепляет статус предпринимателя;</li>
                                                    <li>определяет возможные системы налогообложения;</li>
                                                    <li>определяет необходимость и порядок формирования уставного капитала, число учредителей;</li>
                                                    <li>определяет вид ответственности предпринимателя перед кредиторами;</li>
                                                    <li>определяет механизм контроля со стороны государства.</li>
                                                </ol>

                                            </p>

                                            <p>
                                                Необходимость выбора организационно-правовой формы может возникать в двух случаях: при создании собственного дела (нового предприятия) и в случае преобразования существующего предприятия.
                                            </p>

                                            <p>
                                                <i>Перечень организационно-правовых форм в любом государстве жёстко закреплён законодательно и является конечным. Предприниматель не может зарегистрировать своё предприятие в форме, не предусмотренной законодательством.</i>
                                            </p>

                                            <p>
                                                В РФ такой перечень закреплён в специальном документе — <b>ОКОПФ — Общероссийский классификатор организационно-правовых форм</b>.
                                            </p>

                                            <h3>Перечень организационно-правовых форм</h3>

                                            <p>
                                                Для коммерческой деятельности граждан:
                                                <li>главы крестьянских (фермерских) хозяйств;</li>
                                                <li>индивидуальный предприниматель (ИП).</li>
                                            </p>

                                            <p>
                                                Для юридических лиц, являющихся коммерческими корпоративными организациями:
                                                <li>хозяйственные товарищества (полные и коммандитные);</li>
                                                <li>хозяйственные общества: общества с ограниченной ответственностью (ООО) и акционерные общества (АО), которые делятся на публичные (ПАО) и непубличные (НАО);</li>
                                                <li>производственные кооперативы.</li>
                                            </p>

                                            <h4>Хозяйственные товарищества</h4>

                                            <p>
                                                Хозяйственными товариществами признаются корпоративные коммерческие организации с разделённым на доли участников складочным капиталом. Участники товарищества несут полную ответственность по обязательствам товарищества всем своим имуществом.
                                            </p>

                                            <h4>Хозяйственные общества</h4>

                                            <p>
                                                Хозяйственными обществами признаются корпоративные коммерческие организации с разделённым на вклады учредителей уставным капиталом. Хозяйственные общества могут создаваться в организационно-правовой форме акционерного общества или общества с ограниченной ответственностью. Объём правомочий участников хозяйственного общества определяется пропорционально их долям в уставном капитале общества.
                                            </p>

                                            <h4>Общество с ограниченной ответственностью</h4>

                                            <p>
                                                Прибыль хозяйственного общества распределяется пропорционально вкладам в уставный капитал. Ответственность учредителей ООО ограничена их вкладом в уставный капитал, поэтому в случае банкротства ООО кредиторы смогут востребовать с учредителя только средства в размере его вклада в уставный капитал. Единственной организационно-правовой формой, которая позволяет зарегистрировать организацию одному человеку, является ООО.
                                            </p>

                                            <h4>Публичное акционерное общество (ПАО)</h4>

                                            <p>
                                                Публичным является акционерное общество, акции которого публично размещаются на рынке ценных бумаг. В публичном акционерном обществе образуется коллегиальный орган управления общества, число членов которого не может быть менее пяти. В публичном акционерном обществе не может быть ограничено количество акций, принадлежащих одному акционеру, их суммарная номинальная стоимость, а также максимальное число голосов, предоставляемых одному акционеру. Участники акционерного общества (акционеры) не отвечают по обязательствам АО и несут риск убытков только в размере стоимости принадлежащих им акций.
                                            </p>

                                            <h4>Производственный кооператив</h4>

                                            <p>
                                                Производственным кооперативом (артелью) признаётся добровольное объединение граждан на основе членства для совместной производственной или иной хозяйственной деятельности (производство, переработка, сбыт промышленной, сельскохозяйственной и иной продукции, выполнение работ, торговля, бытовое обслуживание, оказание других услуг), основанной на их личном трудовом и ином участии и объединении его членами (участниками) имущественных паевых взносов. Члены производственного кооператива несут по обязательствам кооператива субсидиарную ответственность в размерах и в порядке, которые предусмотрены законом о производственных кооперативах и уставом кооператива.
                                            </p>
