                                                    <li><b>ПАО</b> — публичное акционерное общество.</li>
                                                    <li><b>Фондовая биржа Санкт-Петербурга (ФБ СПБ)</b>: основной упор на торговлю товарными фьючерсами. Здесь формируются российские цены на товары: зерно, хлопок, кукурузу, топливо и т. д.</li>
                                                    <li><b>Cанкт-Петербургская валютная биржа (СПВБ)</b>: здесь происходит в основном торговля долларами и евро.</li>
                                                </ol>
                                            </p>

                                            <p>
                                                Как мы писали и ранее в лекциях Forex (сокращение от Foreign Exchange) — это международный финансовый рынок, на котором покупается и продаётся валюта. Основными участниками международного валютного рынка Forex являются центральные банки разных стран, а также крупнейшие банки мира. Все остальные игроки, в том числе частные инвесторы, могут подключиться к этому рынку только через длинную цепочку дилеров (участник фондовой биржи или банк, который занимается куплей-продажей ценных бумаг, валюты, драгоценных металлов), брокеров и посредников, не всегда прозрачную. Для рынка FOREX не существует единой биржевой площадки. Этот рынок работает в международном масштабе дней в неделю круглосуточно. Как только заканчиваются торги в одной части мира, в другой они уже начинаются.
                                            </p>

                                            <p>
                                                Доступ на биржу в России частное лицо может получить только через посредника – брокерскую компанию. Выбираете надежного брокера, заключаете с ним договор, открываете брокерский счет и после внесения необходимой суммы начинаете работу трейдером в режиме онлайн. Биржевой рынок России довольно молод и находится в стадии активного развития. Это открывает широкие перспективы для трейдинга. Не упускайте свой шанс.
