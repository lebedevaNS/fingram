                                        <img src="assets/images/plan.png" width="540" height="400">
                                        <p>
                                            В 4 лекции базового уровня мы уже разобрали, что такое бизнес-план и для чего он нужен. Сейчас мы подробно рассмотрим процесс создания бизнес-плана. Бизнес-планирование проводится в несколько этапов:
                                            <li> определение целей разработки бизнес-плана;</li>
                                            <li> определение целевой аудитории бизнес-плана;</li>
                                            <li>определение структуры бизнес-плана;</li>
                                            <li>сбор и систематизация информации для подготовки всех разделов бизнес-плана;</li>
                                            <li>оформление бизнес-плана.</li>
                                        </p>

                                        <p>
                                            <i>
                                                Основная цель разработки бизнес-плана — планирование хозяйственной деятельности фирмы в краткосрочном и долгосрочном периодах в соответствии с её возможностями и потребностями рынка.
                                            </i>
                                        </p>

                                        <p>
                                            Целевой аудиторией бизнес-плана могут быть внешние и внутренние пользователи. <b>Под внутренними пользователями</b> мы понимаем инициаторов бизнес-идеи, учредителей компании, персонал фирмы. <b>К внешним пользователям</b> относятся потенциальные инвесторы (их также называют бизнес-ангелами), кредиторы, партнёры.
                                        </p>

                                        <p>
                                            В процессе бизнес-планирования необходимо:
                                            <ol>
                                                <li>Cобрать и систематизировать всю имеющуюся информацию о том продукте (услуге), который ты планируешь производить (оказывать). Необходимо разобраться во всех тонкостях производства товара, стать профессионалом в своём деле — только в этом случае можно построить успешный долгосрочный бизнес.
                                                </li>
                                                <li>Собрать информацию о рынке сбыта, потенциальных клиентах и конкурентах, изучить перечень товаров и услуг, которые предлагают твои конкуренты. Важно видеть перспективы рынка и уметь прогнозировать спрос.</li>
                                                <li> Изучить слабые и сильные стороны своей компании.</li>
                                                <li> Произвести расчёт финансовых затрат для организации своего бизнеса, понять, из какого источника ты сможешь привлечь эти средства.</li>
                                                <li> Разработать организационную структуру своей компании. Важно понимать, какое количество сотрудников будет работать в твоей компании и какие должности они будут занимать. В этом случае ты сможешь разделить между ними зоны ответственности.</li>
                                            </ol>
                                        </p>

                                        <p>
                                            После сбора и анализа всей предварительной информация можно приступать к составлению бизнес-плана. Структурировать бизнес-план можно по своему усмотрению, не существует жёстко заданных правил. Самое важное здесь то, что разработанный документ должен чётко отвечать на следующие вопросы: производством какой продукции ты планируешь заниматься, откуда ты возьмёшь деньги для запуска своего бизнеса, какие предполагаются затраты, как скоро окупится твой бизнес, когда ты начнёшь получать доходы и в каком объёме. Бизнес-план обычно разрабатывается на долгосрочную перспективу (от до лет). При запуске бизнеса рекомендуется осуществлять планирование по месяцам, кварталам.Рассмотрим ключевые разделы бизнес-плана.
                                        </p>

                                        <p>
                                            <b>Первый раздел — резюме.</b> В резюме должно входить самое полное описание сути твоего бизнеса. Как правило, все инвесторы обращают внимание на этот раздел, поскольку именно здесь сказано, сколько средств необходимо на реализацию бизнеса, указаны прогнозируемые результаты деятельности, сроки окупаемости и риски. Поэтому твоя цель — показать в резюме все преимущества своей бизнес-идеи.
                                        </p>

                                        <p>
                                            <b>Второй раздел — описание предприятия.</b> В этом разделе даются полное и сокращённое наименование предприятия, дата регистрации, описание основных видов деятельности предприятия (коды ОКВЭД), его организационно-правовая форма (ООО, ОАО и т. д.). Описываются цели деятельности созданной компании и стратегии её развития. Цели должны быть чёткими и однозначными, выглядеть реалистично и достижимо.
                                        </p>

                                        <p>
                                            <b>Третий раздел — описание продукции (услуг).</b> В данном разделе подробно описывается производимая предприятием продукция или осуществляемые услуги. Обязательно нужно указать наименование продукции, её индивидуальные особенности, область применения и экологичность, конкурентные преимущества, степень готовности к производству.
                                        </p>

                                        <p>
                                            <b>Четвёртый раздел — организационный план. </b>Организационный план — это часть бизнес-плана организации, в котором указывается состав участников проекта (предприятия) и описывается структура, с помощью которой участники проекта, сотрудничая друг с другом, будут добиваться поставленных целей. В данном разделе должна быть указаны структурные подразделения компании, проведена оценка имеющихся кадров (численный состав), а также определена потребность в привлечении специалистов.
                                        </p>

                                        <p>
                                            <b>Пятый раздел — план-маркетинг.</b> Маркетинг — это процесс, обеспечивающий продвижение и реализацию товара или услуг на рынке. План-маркетинг представляет собой важнейшую составную часть бизнес-плана компании, где устанавливаются рыночные цели предприятия и предлагаются конкретные методы их достижения. Этот раздел содержит маркетинговые исследования, проведённые в отношении рынка сбыта продукции. В разделе описываются потенциальные клиенты, конкуренты, объёмы продаж на рынке, диапазон цен. Разрабатывая стратегию рекламы, очень важно понимать, каким образом ты планируешь выводить товар на рынок (через посредников или через свои точки продаж). План маркетинга обычно разрабатывается сроком на год.
                                        </p>

                                        <p>
                                            <b>Шестой раздел — производственный план.</b> В производственном плане должны быть приведены все производственные или прочие рабочие процессы фирмы. Этот раздел бизнес-плана очень подробно описывает процессы производства и реализации продукции. Здесь же нужно отразить все производственные затраты и привести календарный план производства.
                                        </p>

                                        <p>
                                            <b>Седьмой раздел — финансовый план.</b> В финансовый плане приводится анализ денежного положения предприятия на текущий период и описываются будущие денежные перспективы. Здесь указаны основные финансовые показатели проекта: затраты подготовительного и основных периодов, расчёты финансовых поступлений, расчёты налоговых платежей, финансовые прогнозы. Таким образом, финансовый план содержит обобщённую информацию о доходах и расходах компании, о первоначальных затратах, о структуре денежных потоков. Финансовый план составляется на – лет и включает отчёт о прибыли, отчёт о движении денежных средств, балансовый отчёт, а также группу показателей, характеризующих платёжеспособность и ликвидность, управление активами, соотношение заёмных и собственных средств. При составлении финансового плана анализируется состояние потока реальных денег, устойчивость предприятия, источники и использование средств. В результате определяется точка безубыточности (положение дел, при котором компания не получает ни прибыли, ни убытков) и сроки окупаемости бизнеса.
                                        </p>

                                        <p>
                                            Стоит сказать несколько слов об оформлении бизнес-плана. Бизнес-план — это «лицо» компании, и он должен производить благоприятное впечатление на инвесторов и кредиторов. На титульной странице необходимо указать:
                                            <li>название компании;</li>
                                            <li>название документа («Бизнес-план»);</li>
                                            <li>дату составления или изменения документа;</li>
                                            <li>юридический адрес и номер телефона организации;</li>
                                            <li>сделать пометку о том, что содержание данного бизнес-плана носит конфиденциальный характер.</li>
                                        </p>

                                        <p>
                                            Для представления числовых данных рекомендуем использовать таблицы, графики и диаграммы — это делает материал более наглядным. Бизнес-план обычно просматривают много людей, поэтому необходимо сделать переплёт, соответствующий объёму документа (мягкий переплёт более уместен для небольшого бизнес-плана, а твёрдый — для документа, насчитывающего более 80 страниц).
                                        </p>
