                                            <p>
                                                <i>
                                                    Валютный рынок является частью финансового рынка, на нём совершаются сделки купли-продажи иностранной валюты (ведётся торговля иностранной валютой). Валютный рынок является наиболее популярным финансовым рынком для населения, поскольку практически отсутствуют барьеры для входа на этот рынок.
                                                </i>
                                            </p>
                                            
                                            <img src="assets/images/valrynok.png" width="500" height="400">

                                            <p>
                                                Торговлю валютами на рынке ведут не только Центральные банки и международные финансовые организации, но и частные лица. На валютном рынке, как и на любом другом, действуют законы спроса и предложения: чем выше спрос на товар — тем выше цена товара. А в случае с валютным рынком и товаром, продающимся на нём, — иностранной валютой:
                                                <li>чем выше спрос на иностранную валюту, тем выше её курс;</li>
                                                <li>чем выше предложение иностранной валюты, тем ниже её курс.</li>
                                            </p>

                                            <p>
                                                Спрос на иностранную валюту формируют те участники валютного рынка, которые хотят её купить (приобрести):
                                                <li>физические лица, которые покупают иностранную валюту либо для того, что хранить в ней свои сбережения, либо для поездки за рубеж, либо для покупки товаров за рубежом и т. п.;</li>
                                                <li>организации-импортёры, которые приобретают иностранную валюту для того, чтобы закупить товары за рубежом;</li>
                                                <li>игроки на валютной бирже (как частные лица, так и организации), желающие купить иностранную валюту для её последующей продажи по более высокой цене.</li>
                                            </p>

                                            <p>
                                                Предложение иностранной валюты формируют те участники валютного рынка, которые хотят её продать (конвертировать в другую валюту):
                                                <li>физические лица, которые продают иностранную валюту либо для того, что перевести свои сбережения в национальную или любую другую валюту, либо для того, чтобы перевести иностранную валюту, оставшуюся после поездки за рубеж, в национальную и т. п.;</li>
                                                <li>организации-экспортёры, которые продали свой товар за рубежом, получили иностранную валюту и хотят конвертировать её в национальную валюту для ведения расчётов с бюджетом;</li>
                                                <li>игроки на валютной бирже (как частные лица, так и организации), желающие продать иностранную валюту, поскольку её цена достигла максимума и может, по их мнению, начать падать.</li>
                                            </p>

                                            <p>
                                                <i>Если участники валютного рынка начинают покупать иностранную валюту, то спрос на неё возрастает. Если спрос на иностранную валюту возрастает, то курс её растёт. Если курс иностранной валюты к национальной растёт, то курс национальной валюты снижается.</i>
                                            </p>

                                            <blockquote class="situation" style="margin-bottom: 40px;">
                                                <div class="info_text">
                                                    <p>
                                                        <i>Государство через Центральный банк может управлять изменением курса рубля в определённых пределах (удерживать курс примерно на одном уровне): если курс рубля начинает расти, то Центральный банк начинает продавать рубли, то есть увеличивает предложение рублей, вследствие чего курс рубля начинает падать и в результате стабилизируется.</i>
                                                    </p>
                                                </div>
                                            </blockquote><br>

                                            <p>
                                                При падении курса рубля к доллару (например, доллар стал стоить не , а руб.) выручка отечественных компаний, которые продают свой товар за рубеж за доллары, выразится в большей сумме рублей. Тогда и поступления налогов в государственный бюджет от этих компаний возрастут.
                                            </p>
