                                                <i>
                                                    <b>Торговая платформа</b> является главным инструментом трейдера, она обеспечивает соединение с сервером и позволяет передавать заявки на покупку-продажу ценных бумаг.
                                                </i>
                                            </p>
                                        
                                            <p>
                                                Торговые платформы (торговые системы, или терминалы) устанавливаются на компьютер, планшет, смартфон.
                                            </p>
                                            
                                            <p>
                                                <i>
                                                    <b>Трейдер</b> — сотрудник брокерской фирмы, выполняющий заказы клиентов на куплю-продажу ценных бумаг на бирже.
                                                </i>
                                            </p>

                                            <p>
                                                Существует множество торговых платформ, обеспечивающих выход на различные рынки: фондовый рынок (рынок ценных бумаг), срочный рынок (рынок фьючерсов и опционов, например FORTS), валютный рынок (торги иностранной валютой), товарный рынок (рынок металлов, зерна и т. п.), однако есть платформы, объединяющие все эти рынки, например, QUIK.
                                            </p>

                                            <p>
                                                <i>
                                                    <b>Фьючерс</b> – контракт, согласно которому продавец обязуется поставить покупателю базовый актив по оговорённой цене и в определённый срок.
                                                </i>
                                            </p>

                                            <p>
                                                <i>
                                                    <b>Опцион</b> — договор, по которому покупатель получает право, но не обязательство, совершить покупку или продажу данного актива по заранее оговорённой цене или по нефиксированной цене
                                                </i>
                                            </p>

                                            <p>
                                                Многие брокеры в США для выхода на биржевые площадки (NYSE, NASDAQ, AMEX и т.д.) разрабатывают свои торговые платформы, поскольку им дешевле один раз написать свою платформу, чем регулярно кому-то платить за использование сторонней. Российские брокеры предпочитают пользоваться платформами других компаний. В России для выхода на рынок FOREX в основном используется платформа MetaTrader, а для выхода на Фондовый или Срочный рынок (FORTS) — платформа QUIK. Но для выхода на фондовый рынок США следует использовать другие платформы: ROX или Laser и т. п.
                                            </p>

                                            <p>
                                                Платформы, выдаваемые брокерами частным инвесторам, могут быть платными и бесплатными. Например, торговый терминал QUIK, используемый в России большинством брокеров, часто выдаётся бесплатно, но некоторые брокеры устанавливают ежемесячную плату за его использование (в зависимости от функционала самой платформы).
                                            </p>

                                            <p>
                                                Требования к торговым платформам:
                                                <li> платформа должны быть удобной;</li>
                                                <li>должна быть функциональной (иметь различные индикаторы технического анализа, таблицы, диаграммы и графики актива);</li>
                                                <li>должна обеспечивать доступ на биржи.</li>
                                            </p>

                                            <p>
                                                На фондовом рынке существует множество бирж, причём на каждой из них есть уникальные активы, недоступные для покупки на других площадках. Для начинающих частных инвесторов может оказаться удобной торговая платформа Transaq, созданная компанией «Скрин маркет системз» и предоставляющая широкий функционал для работы, выход на все популярные биржи.
                                            </p>
                                        
                                            <h3>Частный инвестор на рынке ценных бумаг</h3>

                                            <p>
                                                Приобрести ценную бумагу несколько труднее, чем купить простой товар или услугу. Ни в магазине, ни напрямую в Интернете сделать этого нельзя. Для покупки ценной бумаги придётся выйти на рынок особого рода — фондовый рынок, а конкретное место покупки-продажи ценной бумаги называется фондовой биржей.
                                            </p>
                                        
                                            <p>
                                                Осуществлять покупку/продажу ценных бумаг непосредственно на фондовой бирже может только лицензированная организация, поэтому если гражданин принял решение выйти на рынок ценных бумаг, ему понадобится посредник: брокер или доверительный управляющий.
                                            </p>

                                            <p>
                                                Оба эти посредника могут заключить договор с гражданином, но принципиальное различие между брокером и доверительным управляющим заключается в том, кто будет принимать решение о покупке или продаже конкретной ценной бумаги.
                                            </p>

                                            <p>
                                                При заключении брокерского договора все решения о покупке или продаже ценных бумаг принимаются гражданином, а брокер просто исполняет  поручения. При заключении договора доверительного управления все решения о покупке или продаже ценных бумаг принимает доверительный управляющий, а гражданин только предоставляет ему денежные средства, на которые осуществляется приобретение ценных бумаг.
                                            </p>

                                            <p>
                                                В течение торговой сессии (около часов) один и тот же инвестор может неоднократно купить и продать любое количество ценных бумаг. При покупке или продаже любой ценной бумаги (инструмента сделки), насчёт инвестора вносится учётная запись.
                                            </p>
                                        
                                            <p>
                                                <i>
                                                    <b>Торговая сессия</b> — это временной промежуток, в течении которого банки и торговые площадки ведут активные торги на валютном рынке.
                                                </i>
                                            </p>
                                        
                                            <p>
                                                Поскольку инвестор неоднократно за торговую сессию может продавать и покупать ценные бумаги, и курс приобретённых ценных бумаг постоянно изменяется, то счёт инвестора находится в постоянном движении. Инвестор имеет возможность самостоятельно сформировать свой инвестиционный портфель только через брокера, который обеспечивает доступ к торгам на фондовом рынке.
                                            </p>

                                            <p>
                                                Сделки совершаются при помощи специальных компьютерных программ, позволяющих работать на фондовой бирже в режиме реального времени
                                            </p>
